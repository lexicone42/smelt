//! Generate Rust provider code from a resource manifest.
//!
//! Emits schema functions, CRUD implementations, and diff rules that follow
//! the patterns established in the hand-written smelt provider files.

use std::collections::BTreeMap;
use std::fmt::Write;

use crate::manifest::{ApiStyle, AwsBuilderGroup, AwsIdSource, AwsReadStyle, AwsTagStyle, CompositeIdPart, FieldDef, ResourceManifest, Scope};
use crate::{raw_field, safe_ident, snake_case};

pub fn generate_provider_code(manifest: &ResourceManifest) -> String {
    let mut out = String::with_capacity(8192);

    write_header(&mut out, manifest);
    write_schema_fn(&mut out, manifest);
    write_create_fn(&mut out, manifest);
    write_read_fn(&mut out, manifest);
    write_update_fn(&mut out, manifest);
    write_delete_fn(&mut out, manifest);
    write_diff_rules(&mut out, manifest);

    out
}

// ── Header ──────────────────────────────────────────────────────────────────

fn write_header(out: &mut String, m: &ResourceManifest) {
    let _ = writeln!(out, "// Generated by smelt-codegen — do not edit by hand.");
    let _ = writeln!(
        out,
        "// Source manifest: {}.toml",
        m.resource.type_path.replace('.', "_").to_lowercase()
    );
    let _ = writeln!(out, "//");
    let _ = writeln!(out, "// Resource: {}", m.resource.type_path);
    let _ = writeln!(out, "// SDK: {} ({})", m.resource.sdk_crate, m.resource.sdk_model);
    let _ = writeln!(out);

    if m.resource.provider == "gcp" {
        let _ = writeln!(out, "use crate::provider::{{ConfigExt, ProviderError, ResourceOutput}};");
        let _ = writeln!(out, "use std::collections::HashMap;");
    } else {
        let _ = writeln!(out, "use crate::provider::{{ConfigExt, ProviderError, ResourceOutput}};");
        let _ = writeln!(out, "use std::collections::HashMap;");
    }
    let _ = writeln!(out);
}

// ── Schema function ─────────────────────────────────────────────────────────

fn write_schema_fn(out: &mut String, m: &ResourceManifest) {
    let fn_name = schema_fn_name(&m.resource.type_path);
    let sections = group_fields_by_section(&m.fields);

    let _ = writeln!(
        out,
        "pub(super) fn {fn_name}() -> crate::provider::ResourceTypeInfo {{"
    );
    let _ = writeln!(out, "    crate::provider::ResourceTypeInfo {{");
    let _ = writeln!(
        out,
        "        type_path: \"{}\".into(),",
        m.resource.type_path
    );
    let _ = writeln!(
        out,
        "        description: \"{}\".into(),",
        m.resource.description.replace('"', "\\\"")
    );
    let _ = writeln!(out, "        schema: crate::provider::ResourceSchema {{");
    let _ = writeln!(out, "            sections: vec![");

    for (section_name, fields) in &sections {
        let _ = writeln!(out, "                crate::provider::SectionSchema {{");
        let _ = writeln!(out, "                    name: \"{section_name}\".into(),");
        let _ = writeln!(
            out,
            "                    description: \"{} configuration\".into(),",
            capitalize(section_name)
        );
        let _ = writeln!(out, "                    fields: vec![");

        for (field_name, field_def) in fields {
            write_field_schema(out, field_name, field_def);
        }

        let _ = writeln!(out, "                    ],");
        let _ = writeln!(out, "                }},");
    }

    let _ = writeln!(out, "            ],");
    let _ = writeln!(out, "        }},");
    let _ = writeln!(out, "    }}");
    let _ = writeln!(out, "}}");
    let _ = writeln!(out);
}

fn write_field_schema(out: &mut String, name: &str, f: &FieldDef) {
    let _ = writeln!(out, "                        crate::provider::FieldSchema {{");
    let _ = writeln!(out, "                            name: \"{name}\".into(),");
    let _ = writeln!(
        out,
        "                            description: \"{}\".into(),",
        f.description
            .as_deref()
            .unwrap_or("")
            .replace('\\', "\\\\")
            .replace('"', "\\\"")
    );
    let _ = writeln!(
        out,
        "                            field_type: {},",
        field_type_expr(&f.field_type, &f.variants)
    );
    let _ = writeln!(
        out,
        "                            required: {},",
        f.required
    );

    if let Some(ref default) = f.default {
        let _ = writeln!(
            out,
            "                            default: Some(serde_json::json!({})),",
            toml_to_json_literal(default)
        );
    } else {
        let _ = writeln!(out, "                            default: None,");
    }

    let _ = writeln!(
        out,
        "                            sensitive: {},",
        f.sensitive
    );
    let _ = writeln!(out, "                        }},");
}

// ── Create function ─────────────────────────────────────────────────────────

fn write_create_fn(out: &mut String, m: &ResourceManifest) {
    let fn_name = crud_fn_name("create", &m.resource.type_path);
    let is_gcp = m.resource.provider == "gcp";

    let _ = writeln!(out, "pub(super) async fn {fn_name}(");
    let _ = writeln!(out, "    &self,");
    let _ = writeln!(out, "    config: &serde_json::Value,");
    let _ = writeln!(out, ") -> Result<ResourceOutput, ProviderError> {{");

    // Extract fields from config
    let sdk_crate_mod = sdk_crate_to_mod(&m.resource.sdk_crate);
    let _ = writeln!(out, "    // Extract fields from config");
    for (name, field) in &m.fields {
        if field.output_only || field.skip {
            continue;
        }
        // Skip extraction for skip_create fields unless they have a post-create method
        // (post-create fields still need their values extracted)
        if field.skip_create && field.aws_post_create_method.is_none() {
            continue;
        }
        write_field_extraction(out, name, field, &sdk_crate_mod);
    }
    let _ = writeln!(out);

    if is_gcp {
        write_gcp_create_body(out, m);
    } else {
        write_aws_create_body(out, m);
    }

    let _ = writeln!(out, "}}");
    let _ = writeln!(out);
}

fn write_gcp_create_body(out: &mut String, m: &ResourceManifest) {
    let model = &m.resource.sdk_model;
    let sdk_crate_mod = sdk_crate_to_mod(&m.resource.sdk_crate);
    let client_accessor = m.resource.client_accessor.clone().unwrap_or_else(|| snake_case(&m.resource.sdk_client));
    let create_method = &m.crud.create;
    let is_direct_model = m.resource.api_style == ApiStyle::DirectModel;
    let skip_name = m.resource.skip_name_on_create;
    let full_name_on_model = m.resource.full_name_on_model;

    // Extract labels if any identity.labels field
    if m.fields.contains_key("labels") {
        if m.resource.raw_labels {
            // Raw labels: extract directly without injecting managed_by.
            // Used for resources where labels are type-specific config (e.g., NotificationChannel).
            let _ = writeln!(out, "    // NotificationChannel.labels are channel-type-specific config fields");
            let _ = writeln!(out, "    // (e.g. email_address for email channels), NOT user labels.");
            let _ = writeln!(out, "    // Do NOT inject managed_by — use the user's labels as-is.");
            let _ = writeln!(out, "    let labels: HashMap<String, String> = config");
            let _ = writeln!(out, "        .pointer(\"/identity/labels\")");
            let _ = writeln!(out, "        .and_then(|v| v.as_object())");
            let _ = writeln!(out, "        .map(|m| {{");
            let _ = writeln!(out, "            m.iter()");
            let _ = writeln!(out, "                .filter_map(|(k, v)| v.as_str().map(|s| (k.clone(), s.to_string())))");
            let _ = writeln!(out, "                .collect()");
            let _ = writeln!(out, "        }})");
            let _ = writeln!(out, "        .unwrap_or_default();");
        } else {
            let _ = writeln!(out, "    let labels = super::extract_labels(config);");
        }
    }

    // When skip_name_on_create and no resource_id_setter, name variable is unused
    if skip_name && m.resource.resource_id_setter.is_none() {
        let _ = writeln!(out, "    let _ = &name; // server-assigned name — not set on model");
    }

    if is_direct_model {
        // Direct model style: setters go on the request builder directly
        // Build the full resource name for the name field
        let _ = writeln!(out, "    // Build full resource name");
        let parent = build_parent_expr(m);
        let noun = snake_case(&m.resource.sdk_model);
        let noun_plural = format!("{noun}s");
        let _ = writeln!(
            out,
            "    let full_name = format!(\"{{}}/{noun_plural}/{{}}\", {parent}, name);"
        );
        let _ = writeln!(out);
        let _ = writeln!(out, "    // Make API call (direct model: setters on request builder)");
        let _ = writeln!(out, "    let result = self.{client_accessor}().await?");
        let _ = writeln!(out, "        .{create_method}()");
        let _ = writeln!(out, "        .set_name(&full_name)");

        // Set remaining fields directly on the request builder
        for (name, field) in &m.fields {
            if field.output_only || field.skip || name == "labels" || name == "name" {
                continue;
            }
            let sdk_field = field.sdk_field.as_deref().unwrap_or(name.as_str());
            let setter = format!("set_{sdk_field}");
            write_request_setter(out, name, &setter, field);
        }

        // Labels
        if m.fields.contains_key("labels") {
            let _ = writeln!(out, "        .set_labels(labels)");
        }
    } else {
        let _ = writeln!(out, "    // Build SDK model");
        let _ = writeln!(
            out,
            "    let mut model = {sdk_crate_mod}::model::{model}::default();"
        );

        // For full_name_on_model: compute full resource path before model setters
        if full_name_on_model {
            let parent = build_parent_expr(m);
            let noun_plural = resource_noun_plural(m);
            let _ = writeln!(out, "    let full_name = format!(\"{{}}/{noun_plural}/{{}}\", {parent}, name);");
        }

        // Set fields on the model
        for (name, field) in &m.fields {
            if field.output_only || field.skip || name == "labels" {
                continue;
            }
            // skip_name_on_create: don't set model.name — GCP auto-assigns it
            if name == "name" && skip_name {
                let _ = writeln!(out, "    // Do NOT set model.name on create — GCP auto-assigns it.");
                continue;
            }
            // full_name_on_model: set full resource path instead of short name
            if name == "name" && full_name_on_model {
                let _ = writeln!(out, "    model = model.set_name(&full_name);");
                continue;
            }
            let sdk_field = field.sdk_field.as_deref().unwrap_or(name);
            let setter = format!("set_{sdk_field}");
            write_model_setter(out, name, &setter, field, &sdk_crate_mod);
        }

        // Labels
        if m.fields.contains_key("labels") {
            let _ = writeln!(out, "    model = model.set_labels(labels);");
        }

        let _ = writeln!(out);
        let _ = writeln!(out, "    // Make API call");

        if m.resource.api_style == ApiStyle::ResourceName {
            // Resource-name style: set_parent + set_resource_id + set_resource(model)
            let parent = build_parent_expr(m);
            let parent_setter = m.resource.parent_setter.as_deref().unwrap_or("set_parent");
            let _ = writeln!(out, "    let parent = {parent};");

            // When skip_name_on_create, capture response to get server-assigned name
            if skip_name {
                let _ = writeln!(out, "    let created = self.{client_accessor}().await?");
            } else {
                let _ = writeln!(out, "    self.{client_accessor}().await?");
            }
            let _ = writeln!(out, "        .{create_method}()");
            let _ = writeln!(out, "        .{parent_setter}(&parent)");

            if let Some(ref id_setter) = m.resource.resource_id_setter {
                let _ = writeln!(out, "        .{id_setter}(&name)");
            }

            let body_setter = m.resource.resource_body_setter.as_deref().unwrap_or("set_body");
            let _ = writeln!(out, "        .{body_setter}(model)");
        } else {
            // Compute style: set_project + set_zone/region + set_body
            match m.resource.scope {
                Scope::Zonal => {
                    let _ = writeln!(out, "    let zone = config.str_or(\"/sizing/zone\", &format!(\"{{}}-a\", self.region)).to_string();");
                }
                Scope::Regional => {
                    let _ = writeln!(out, "    let region = config.str_or(\"/network/region\", &self.region).to_string();");
                }
                _ => {}
            }
            let _ = writeln!(out, "    self.{client_accessor}().await?");
            let _ = writeln!(out, "        .{create_method}()");
            let _ = writeln!(out, "        .set_project(&self.project_id)");

            match m.resource.scope {
                Scope::Zonal => {
                    let _ = writeln!(out, "        .set_zone(&zone)");
                }
                Scope::Regional => {
                    let _ = writeln!(out, "        .set_region(&region)");
                }
                _ => {}
            }

            let _ = writeln!(out, "        .set_body(model)");
        }
    }

    if m.resource.lro_create {
        let _ = writeln!(out, "        .poller()");
        let _ = writeln!(out, "        .until_done()");
        let _ = writeln!(out, "        .await");
    } else {
        let _ = writeln!(out, "        .send()");
        let _ = writeln!(out, "        .await");
    }
    let _ = writeln!(
        out,
        "        .map_err(|e| super::classify_gcp_error(\"{} {}\", e))?;",
        capitalize(&m.crud.create),
        m.resource.sdk_model
    );
    let _ = writeln!(out);

    // Build provider_id and read back
    if skip_name {
        // Server-assigned name: get provider_id from create response
        let _ = writeln!(out, "    let provider_id = created.name.clone();");
    } else {
        write_provider_id_construction(out, m);
    }
    let read_fn = crud_fn_name("read", &m.resource.type_path);
    if skip_name {
        // Server-assigned resources may have eventual consistency — retry read-back
        let _ = writeln!(out, "    // Retry read-back: some APIs (e.g. IAM) have eventual consistency");
        let _ = writeln!(out, "    for attempt in 0..3u32 {{");
        let _ = writeln!(out, "        match self.{read_fn}(&provider_id).await {{");
        let _ = writeln!(out, "            Ok(result) => return Ok(result),");
        let _ = writeln!(out, "            Err(ProviderError::NotFound(_)) if attempt < 2 => {{");
        let _ = writeln!(out, "                tokio::time::sleep(std::time::Duration::from_millis(500 * (attempt as u64 + 1))).await;");
        let _ = writeln!(out, "            }}");
        let _ = writeln!(out, "            Err(e) => return Err(e),");
        let _ = writeln!(out, "        }}");
        let _ = writeln!(out, "    }}");
        let _ = writeln!(out, "    self.{read_fn}(&provider_id).await");
    } else {
        let _ = writeln!(out, "    self.{read_fn}(&provider_id).await");
    }
}

fn write_aws_create_body(out: &mut String, m: &ResourceManifest) {
    let client_field = m.resource.aws_client_field.as_deref().unwrap_or("client");
    let create_method = snake_case(&m.crud.create);
    let model_name = &m.resource.sdk_model;
    let id_source = m.resource.aws_id_source.clone().unwrap_or(AwsIdSource::ConfigName);
    let tag_style = m.resource.aws_tag_style.clone().unwrap_or(AwsTagStyle::None);

    // Tags
    match &tag_style {
        AwsTagStyle::TagSpecification => {
            let rt = m.resource.aws_tag_resource_type.as_deref().unwrap_or(model_name);
            let sdk_crate_mod = sdk_crate_to_mod(&m.resource.sdk_crate);
            let _ = writeln!(out, "    let tag_spec = Self::build_tags(config, {sdk_crate_mod}::types::ResourceType::{rt});");
        }
        AwsTagStyle::SetTags | AwsTagStyle::TypedTags | AwsTagStyle::InlineKv => {
            let _ = writeln!(out, "    let tags = super::extract_tags(config);");
        }
        AwsTagStyle::PostCreate | AwsTagStyle::None => {}
    }
    let _ = writeln!(out);

    // Check if we need `mut` — needed when there are optional fields, tags, StringArray fields, or builder groups
    let has_builder_groups = !m.resource.aws_builder_groups.is_empty();
    let has_optional_create_fields = m.fields.iter().any(|(_, f)| {
        !f.output_only && !f.skip && !f.required && !f.skip_create && f.aws_builder_group.is_none()
    });
    let has_string_array_fields = m.fields.iter().any(|(_, f)| {
        !f.output_only && !f.skip && !f.skip_create && f.field_type == "StringArray" && f.aws_builder_group.is_none()
    });
    let has_create_tags = !matches!(tag_style, AwsTagStyle::PostCreate | AwsTagStyle::None);
    let needs_mut = has_optional_create_fields || has_create_tags || has_string_array_fields || has_builder_groups;
    let let_binding = if needs_mut { "let mut req" } else { "let req" };

    // Build request — required fields go inline on the builder chain
    let _ = writeln!(out, "    {let_binding} = self.{client_field}");
    let _ = writeln!(out, "        .{create_method}()");

    for (name, field) in &m.fields {
        if field.output_only || field.skip || !field.required || field.skip_create || field.aws_builder_group.is_some() {
            continue;
        }
        let sdk_param = field.sdk_field.as_deref().unwrap_or(name);
        let setter = snake_case(sdk_param);
        let var = safe_ident(name);

        if field.aws_enum {
            // SDK expects an enum type — pass the string value which converts via From<&str>
            let sdk_crate_mod = sdk_crate_to_mod(&m.resource.sdk_crate);
            let enum_type = field.aws_enum_type.as_deref()
                .map(String::from)
                .unwrap_or_else(|| capitalize(sdk_param));
            let _ = writeln!(out, "        .{setter}({sdk_crate_mod}::types::{enum_type}::from({var}.as_str()))");
        } else {
            match field.field_type.as_str() {
                "String" => {
                    let _ = writeln!(out, "        .{setter}(&{var})");
                }
                "Integer" | "Integer_u32" | "Integer_u64" => {
                    let _ = writeln!(out, "        .{setter}({var} as i32)");
                }
                "Float" => {
                    let _ = writeln!(out, "        .{setter}({var})");
                }
                "Bool" => {
                    let _ = writeln!(out, "        .{setter}({var})");
                }
                _ => {}
            }
        }
    }

    let _ = writeln!(out, "    ;");
    let _ = writeln!(out);

    // Set optional fields — distinguished by whether they have defaults
    for (name, field) in &m.fields {
        if field.output_only || field.skip || field.required || field.skip_create || field.aws_builder_group.is_some() {
            continue;
        }
        let sdk_param = field.sdk_field.as_deref().unwrap_or(name);
        let setter = snake_case(sdk_param);
        let var = safe_ident(name);

        let has_default = field.default.is_some();

        // Fields with aws_attr_key use .attributes(key, value) instead of typed setters
        // Add .into() only when aws_attr_name_type is set (SDK uses a typed enum like QueueAttributeName)
        if let Some(ref attr_key) = field.aws_attr_key {
            let into_suffix = if m.resource.aws_attr_name_type.is_some() { ".into()" } else { "" };
            match field.field_type.as_str() {
                "Bool" => {
                    if has_default {
                        let _ = writeln!(out, "    if {var} {{");
                        let _ = writeln!(out, "        req = req.attributes(\"{attr_key}\"{into_suffix}, \"true\");");
                        let _ = writeln!(out, "    }}");
                    } else {
                        let _ = writeln!(out, "    if let Some(true) = {var} {{");
                        let _ = writeln!(out, "        req = req.attributes(\"{attr_key}\"{into_suffix}, \"true\");");
                        let _ = writeln!(out, "    }}");
                    }
                }
                "String" => {
                    if has_default {
                        let _ = writeln!(out, "    req = req.attributes(\"{attr_key}\"{into_suffix}, &{var});");
                    } else {
                        let _ = writeln!(out, "    if let Some(ref v) = {var} {{");
                        let _ = writeln!(out, "        req = req.attributes(\"{attr_key}\"{into_suffix}, v);");
                        let _ = writeln!(out, "    }}");
                    }
                }
                "Integer" | "Integer_u32" | "Integer_u64" => {
                    if has_default {
                        let _ = writeln!(out, "    req = req.attributes(\"{attr_key}\"{into_suffix}, {var}.to_string());");
                    } else {
                        let _ = writeln!(out, "    if let Some(v) = {var} {{");
                        let _ = writeln!(out, "        req = req.attributes(\"{attr_key}\"{into_suffix}, v.to_string());");
                        let _ = writeln!(out, "    }}");
                    }
                }
                _ => {}
            }
            continue;
        }

        if field.aws_enum {
            // SDK enum type — convert from string via From<&str>
            let sdk_crate_mod = sdk_crate_to_mod(&m.resource.sdk_crate);
            let enum_type = field.aws_enum_type.as_deref()
                .map(String::from)
                .unwrap_or_else(|| capitalize(sdk_param));
            if has_default {
                let _ = writeln!(out, "    req = req.{setter}({sdk_crate_mod}::types::{enum_type}::from({var}.as_str()));");
            } else {
                let _ = writeln!(out, "    if let Some(ref v) = {var} {{");
                let _ = writeln!(out, "        req = req.{setter}({sdk_crate_mod}::types::{enum_type}::from(v.as_str()));");
                let _ = writeln!(out, "    }}");
            }
            continue;
        }

        // Builder wrapper: field value is wrapped in a nested builder type
        // e.g., scan_on_push → .image_scanning_configuration(ImageScanningConfiguration::builder().scan_on_push(v).build())
        if let (Some(wrapper), Some(wrapper_type)) = (&field.aws_builder_wrapper, &field.aws_builder_type) {
            let sdk_crate_mod = sdk_crate_to_mod(&m.resource.sdk_crate);
            let wrapper_setter = snake_case(wrapper);
            match field.field_type.as_str() {
                "Bool" => {
                    if has_default {
                        let _ = writeln!(out, "    req = req.{wrapper_setter}(");
                        let _ = writeln!(out, "        {sdk_crate_mod}::types::{wrapper_type}::builder()");
                        let _ = writeln!(out, "            .{setter}({var})");
                        let _ = writeln!(out, "            .build()");
                        let _ = writeln!(out, "    );");
                    } else {
                        let _ = writeln!(out, "    if let Some(v) = {var} {{");
                        let _ = writeln!(out, "        req = req.{wrapper_setter}(");
                        let _ = writeln!(out, "            {sdk_crate_mod}::types::{wrapper_type}::builder()");
                        let _ = writeln!(out, "                .{setter}(v)");
                        let _ = writeln!(out, "                .build()");
                        let _ = writeln!(out, "        );");
                        let _ = writeln!(out, "    }}");
                    }
                }
                "String" => {
                    if has_default {
                        let _ = writeln!(out, "    req = req.{wrapper_setter}(");
                        let _ = writeln!(out, "        {sdk_crate_mod}::types::{wrapper_type}::builder()");
                        let _ = writeln!(out, "            .{setter}(&{var})");
                        let _ = writeln!(out, "            .build()");
                        let _ = writeln!(out, "    );");
                    } else {
                        let _ = writeln!(out, "    if let Some(ref v) = {var} {{");
                        let _ = writeln!(out, "        req = req.{wrapper_setter}(");
                        let _ = writeln!(out, "            {sdk_crate_mod}::types::{wrapper_type}::builder()");
                        let _ = writeln!(out, "                .{setter}(v)");
                        let _ = writeln!(out, "                .build()");
                        let _ = writeln!(out, "        );");
                        let _ = writeln!(out, "    }}");
                    }
                }
                "Integer" | "Integer_u32" | "Integer_u64" => {
                    if has_default {
                        let _ = writeln!(out, "    req = req.{wrapper_setter}(");
                        let _ = writeln!(out, "        {sdk_crate_mod}::types::{wrapper_type}::builder()");
                        let _ = writeln!(out, "            .{setter}({var} as i32)");
                        let _ = writeln!(out, "            .build()");
                        let _ = writeln!(out, "    );");
                    } else {
                        let _ = writeln!(out, "    if let Some(v) = {var} {{");
                        let _ = writeln!(out, "        req = req.{wrapper_setter}(");
                        let _ = writeln!(out, "            {sdk_crate_mod}::types::{wrapper_type}::builder()");
                        let _ = writeln!(out, "                .{setter}(v as i32)");
                        let _ = writeln!(out, "                .build()");
                        let _ = writeln!(out, "        );");
                        let _ = writeln!(out, "    }}");
                    }
                }
                _ => {
                    let _ = writeln!(out, "    // TODO: builder wrapper for {name} ({wrapper_type})");
                }
            }
            continue;
        }

        match field.field_type.as_str() {
            "String" => {
                if has_default {
                    // Extracted as String via str_or — set directly
                    let _ = writeln!(out, "    req = req.{setter}(&{var});");
                } else {
                    // Extracted as Option<String> via optional_str
                    let _ = writeln!(out, "    if let Some(ref v) = {var} {{");
                    let _ = writeln!(out, "        req = req.{setter}(v);");
                    let _ = writeln!(out, "    }}");
                }
            }
            "Integer" | "Integer_u32" | "Integer_u64" => {
                if has_default {
                    // Extracted as i64 via i64_or — set directly
                    let _ = writeln!(out, "    req = req.{setter}({var} as i32);");
                } else {
                    let _ = writeln!(out, "    if let Some(v) = {var} {{");
                    let _ = writeln!(out, "        req = req.{setter}(v as i32);");
                    let _ = writeln!(out, "    }}");
                }
            }
            "Float" => {
                if has_default {
                    let _ = writeln!(out, "    req = req.{setter}({var});");
                } else {
                    let _ = writeln!(out, "    if let Some(v) = {var} {{");
                    let _ = writeln!(out, "        req = req.{setter}(v);");
                    let _ = writeln!(out, "    }}");
                }
            }
            "Bool" => {
                if has_default {
                    let _ = writeln!(out, "    req = req.{setter}({var});");
                } else {
                    let _ = writeln!(out, "    if let Some(v) = {var} {{");
                    let _ = writeln!(out, "        req = req.{setter}(v);");
                    let _ = writeln!(out, "    }}");
                }
            }
            "StringArray" => {
                // StringArray: already handled below in the StringArray-specific loop
            }
            _ => {
                let _ = writeln!(out, "    // TODO: set optional {name} via .{setter}()");
            }
        }
    }

    // Set StringArray fields (both required and optional) — these use a for loop
    for (name, field) in &m.fields {
        if field.output_only || field.skip || field.skip_create || field.field_type != "StringArray" || field.aws_builder_group.is_some() {
            continue;
        }
        let sdk_param = field.sdk_field.as_deref().unwrap_or(name);
        let setter = snake_case(sdk_param);
        let var = safe_ident(name);
        if field.required {
            // Required: extracted as Vec<String>
            let _ = writeln!(out, "    for v in &{var} {{");
            let _ = writeln!(out, "        req = req.{setter}(v);");
            let _ = writeln!(out, "    }}");
        } else {
            // Optional: extracted as Option<Vec<String>>
            let _ = writeln!(out, "    if let Some(ref arr) = {var} {{");
            let _ = writeln!(out, "        for v in arr {{");
            let _ = writeln!(out, "            req = req.{setter}(v);");
            let _ = writeln!(out, "        }}");
            let _ = writeln!(out, "    }}");
        }
    }

    // Apply tags
    match &tag_style {
        AwsTagStyle::TagSpecification => {
            let _ = writeln!(out, "    req = req.tag_specifications(tag_spec);");
        }
        AwsTagStyle::SetTags => {
            let _ = writeln!(out, "    req = req.set_tags(Some(tags));");
        }
        AwsTagStyle::TypedTags => {
            let sdk_crate_mod = sdk_crate_to_mod(&m.resource.sdk_crate);
            let _ = writeln!(out, "    for (k, v) in &tags {{");
            if m.resource.aws_tag_infallible {
                let _ = writeln!(out, "        req = req.tags(");
                let _ = writeln!(out, "            {sdk_crate_mod}::types::Tag::builder()");
                let _ = writeln!(out, "                .key(k)");
                let _ = writeln!(out, "                .value(v)");
                let _ = writeln!(out, "                .build()");
                let _ = writeln!(out, "        );");
            } else {
                let _ = writeln!(out, "        req = req.tags(");
                let _ = writeln!(out, "            {sdk_crate_mod}::types::Tag::builder()");
                let _ = writeln!(out, "                .key(k)");
                let _ = writeln!(out, "                .value(v)");
                let _ = writeln!(out, "                .build()");
                let _ = writeln!(out, "                .map_err(|e| ProviderError::InvalidConfig(format!(\"failed to build Tag: {{e}}\")))?");
                let _ = writeln!(out, "        );");
            }
            let _ = writeln!(out, "    }}");
        }
        AwsTagStyle::InlineKv => {
            let _ = writeln!(out, "    for (k, v) in &tags {{");
            let _ = writeln!(out, "        req = req.tags(k, v);");
            let _ = writeln!(out, "    }}");
        }
        AwsTagStyle::PostCreate | AwsTagStyle::None => {}
    }

    // Build and set builder groups (multi-field nested builders)
    for group_name in all_builder_group_names(m) {
        write_builder_group_create(out, m, &group_name);
    }
    let _ = writeln!(out);

    // Send request
    let read_fn = crud_fn_name("read", &m.resource.type_path);
    match id_source {
        AwsIdSource::ConfigName => {
            // Don't need the result — just fire and check for errors
            let _ = writeln!(out, "    req");
            let _ = writeln!(out, "        .send()");
            let _ = writeln!(out, "        .await");
            let _ = writeln!(
                out,
                "        .map_err(|e| ProviderError::ApiError(format!(\"{create_method}: {{e}}\")))?;"
            );
            let _ = writeln!(out);

            if !m.resource.aws_composite_id.is_empty() {
                // Composite provider_id: assemble from config variables
                write_composite_id_assembly(out, m);
                let _ = writeln!(out);

                // Post-create actions
                write_aws_post_create_actions(out, m, "provider_id");

                let _ = writeln!(out, "    self.{read_fn}(&provider_id).await");
            } else {
                // Post-create actions (fields that need separate API calls)
                write_aws_post_create_actions(out, m, "name");

                let _ = writeln!(out, "    self.{read_fn}(&name).await");
            }
        }
        AwsIdSource::ResponseField => {
            // Need to capture result to extract provider_id
            let _ = writeln!(out, "    let result = req");
            let _ = writeln!(out, "        .send()");
            let _ = writeln!(out, "        .await");
            let _ = writeln!(
                out,
                "        .map_err(|e| ProviderError::ApiError(format!(\"{create_method}: {{e}}\")))?;"
            );
            let _ = writeln!(out);

            // If response has a nested container (e.g., result.user_pool()?.id()),
            // extract through it first.
            // aws_create_no_container: some APIs (e.g., ACM request_certificate) return
            // the ID field directly on the response, not inside a container — even though
            // the read response uses a container (e.g., describe_certificate → .certificate()).
            let field = m.resource.aws_response_id_field.as_deref().unwrap_or("id");
            let accessor = snake_case(field);
            let non_opt = m.resource.aws_response_id_non_optional;
            let use_container = !m.resource.aws_create_no_container;
            if let Some(ref list_acc) = m.resource.aws_create_list_accessor {
                // Create response wraps result in a list: .{list}().first()?.{id}()
                let _ = writeln!(out, "    let container = result");
                let _ = writeln!(out, "        .{list_acc}()");
                let _ = writeln!(out, "        .first()");
                let _ = writeln!(out, "        .ok_or_else(|| ProviderError::ApiError(\"{create_method} returned no {list_acc}\".into()))?;");
                let _ = writeln!(out, "    let provider_id = container");
                let _ = writeln!(out, "        .{accessor}()");
                if !non_opt {
                    let _ = writeln!(out, "        .ok_or_else(|| ProviderError::ApiError(\"{create_method} returned no {field}\".into()))?;");
                } else {
                    let _ = writeln!(out, "    ;");
                }
            } else if use_container && m.resource.aws_response_accessor.is_some() {
                let container = m.resource.aws_response_accessor.as_deref().unwrap();
                let _ = writeln!(out, "    let container = result");
                let _ = writeln!(out, "        .{container}()");
                if non_opt {
                    let _ = writeln!(out, "    ;");
                } else {
                    let _ = writeln!(out, "        .ok_or_else(|| ProviderError::ApiError(\"{create_method} returned no {container}\".into()))?;");
                }
                let _ = writeln!(out, "    let provider_id = container");
                let _ = writeln!(out, "        .{accessor}()");
                if !non_opt {
                    let _ = writeln!(out, "        .ok_or_else(|| ProviderError::ApiError(\"{create_method} returned no {field}\".into()))?;");
                } else {
                    let _ = writeln!(out, "    ;");
                }
            } else {
                let _ = writeln!(out, "    let provider_id = result");
                let _ = writeln!(out, "        .{accessor}()");
                if !non_opt {
                    let _ = writeln!(out, "        .ok_or_else(|| ProviderError::ApiError(\"{create_method} returned no {field}\".into()))?;");
                } else {
                    let _ = writeln!(out, "    ;");
                }
            }
            let _ = writeln!(out);

            // Post-create actions (fields that need separate API calls)
            write_aws_post_create_actions(out, m, "provider_id");

            let _ = writeln!(out, "    self.{read_fn}(provider_id).await");
        }
    }
}

/// Emit post-create API calls for fields with `skip_create = true` + `aws_post_create_method`.
fn write_aws_post_create_actions(out: &mut String, m: &ResourceManifest, id_var: &str) {
    let client_field = m.resource.aws_client_field.as_deref().unwrap_or("client");
    let id_param = m.resource.aws_id_param.as_deref().unwrap_or("name");
    let id_setter = snake_case(id_param);

    for (name, field) in &m.fields {
        if !field.skip_create {
            continue;
        }
        let Some(ref method) = field.aws_post_create_method else {
            continue;
        };
        let sdk_param = field.sdk_field.as_deref().unwrap_or(name);
        let setter = snake_case(sdk_param);
        let var = safe_ident(name);
        let method_snake = snake_case(method);

        // Emit conditional post-create call
        match field.field_type.as_str() {
            "Integer" | "Integer_u32" | "Integer_u64" => {
                let default_val = field.default.as_ref()
                    .and_then(|v| v.as_integer())
                    .unwrap_or(0);
                let _ = writeln!(out, "    if {var} != {default_val} {{");
                let _ = writeln!(out, "        self.{client_field}");
                let _ = writeln!(out, "            .{method_snake}()");
                let _ = writeln!(out, "            .{id_setter}(&{id_var})");
                let _ = writeln!(out, "            .{setter}({var} as i32)");
                let _ = writeln!(out, "            .send()");
                let _ = writeln!(out, "            .await");
                let _ = writeln!(
                    out,
                    "            .map_err(|e| ProviderError::ApiError(format!(\"{method_snake}: {{e}}\")))?;"
                );
                let _ = writeln!(out, "    }}");
            }
            "String" => {
                let _ = writeln!(out, "    if !{var}.is_empty() {{");
                let _ = writeln!(out, "        self.{client_field}");
                let _ = writeln!(out, "            .{method_snake}()");
                let _ = writeln!(out, "            .{id_setter}(&{id_var})");
                let _ = writeln!(out, "            .{setter}(&{var})");
                let _ = writeln!(out, "            .send()");
                let _ = writeln!(out, "            .await");
                let _ = writeln!(
                    out,
                    "            .map_err(|e| ProviderError::ApiError(format!(\"{method_snake}: {{e}}\")))?;"
                );
                let _ = writeln!(out, "    }}");
            }
            "Bool" => {
                let _ = writeln!(out, "    if {var} {{");
                let _ = writeln!(out, "        self.{client_field}");
                let _ = writeln!(out, "            .{method_snake}()");
                let _ = writeln!(out, "            .{id_setter}(&{id_var})");
                let _ = writeln!(out, "            .{setter}({var})");
                let _ = writeln!(out, "            .send()");
                let _ = writeln!(out, "            .await");
                let _ = writeln!(
                    out,
                    "            .map_err(|e| ProviderError::ApiError(format!(\"{method_snake}: {{e}}\")))?;"
                );
                let _ = writeln!(out, "    }}");
            }
            _ => {
                let _ = writeln!(out, "    // TODO: post-create {method_snake} for {name}");
            }
        }
        let _ = writeln!(out);
    }
}

// ── Composite provider_id helpers ────────────────────────────────────────────

/// Emit code to parse a composite provider_id (e.g., "cluster:nodegroup") into parts.
fn write_composite_id_parsing(out: &mut String, parts: &[CompositeIdPart]) {
    let n = parts.len();
    let _ = writeln!(out, "    let id_parts: Vec<&str> = provider_id.split(':').collect();");
    let _ = writeln!(out, "    if id_parts.len() != {n} {{");
    let _ = writeln!(out, "        return Err(ProviderError::InvalidConfig(");
    let part_names: Vec<&str> = parts.iter().map(|p| p.setter.as_str()).collect();
    let format_str = part_names.join(":");
    let _ = writeln!(out, "            format!(\"provider_id must be {format_str}, got: {{provider_id}}\")");
    let _ = writeln!(out, "        ));");
    let _ = writeln!(out, "    }}");
    for (i, part) in parts.iter().enumerate() {
        let var = safe_ident(&snake_case(&part.setter));
        let _ = writeln!(out, "    let {var} = id_parts[{i}];");
    }
    let _ = writeln!(out);
}

/// Emit setter calls for composite ID parts on a builder chain (e.g., `.cluster_name(cluster_name)`).
fn write_composite_id_setters(out: &mut String, parts: &[CompositeIdPart]) {
    for part in parts {
        let setter = snake_case(&part.setter);
        let var = safe_ident(&snake_case(&part.setter));
        let _ = writeln!(out, "        .{setter}({var})");
    }
}

/// Emit code to assemble a composite provider_id from config variables in the create path.
fn write_composite_id_assembly(out: &mut String, m: &ResourceManifest) {
    let parts = &m.resource.aws_composite_id;
    let mut format_parts = Vec::new();
    let mut args = Vec::new();
    for part in parts {
        format_parts.push("{}");
        // Find the field with matching sdk_param or name
        let var = find_composite_field_var(m, &part.field_name);
        args.push(var);
    }
    let format_str = format_parts.join(":");
    let args_str = args.join(", ");
    let _ = writeln!(out, "    let provider_id = format!(\"{format_str}\", {args_str});");
}

/// Find the variable name for a composite ID part in the create path.
/// Looks for a field whose name or sdk_param matches the part's field_name.
fn find_composite_field_var(m: &ResourceManifest, field_name: &str) -> String {
    // Direct field name match
    if m.fields.contains_key(field_name) {
        return safe_ident(field_name);
    }
    // Match by sdk_param
    for (name, field) in &m.fields {
        if let Some(ref sdk) = field.sdk_field {
            if sdk == field_name {
                return safe_ident(name);
            }
        }
    }
    // Fallback: use the field_name directly
    safe_ident(field_name)
}

// ── Builder group helpers ─────────────────────────────────────────────────────

/// Find the builder group definition for a given group name.
fn find_builder_group<'a>(m: &'a ResourceManifest, group_name: &str) -> Option<&'a AwsBuilderGroup> {
    m.resource.aws_builder_groups.iter().find(|g| g.group == group_name)
}

/// Collect fields belonging to a specific builder group, preserving BTreeMap order.
fn group_fields<'a>(m: &'a ResourceManifest, group_name: &str) -> Vec<(&'a str, &'a FieldDef)> {
    m.fields.iter()
        .filter(|(_, f)| f.aws_builder_group.as_deref() == Some(group_name))
        .map(|(name, f)| (name.as_str(), f))
        .collect()
}

/// Get all unique builder group names referenced by fields.
fn all_builder_group_names(m: &ResourceManifest) -> Vec<String> {
    let mut names: Vec<String> = m.fields.values()
        .filter_map(|f| f.aws_builder_group.clone())
        .collect::<std::collections::BTreeSet<_>>()
        .into_iter()
        .collect();
    names.sort();
    names
}

/// Emit code to build a nested builder type from grouped fields on create.
fn write_builder_group_create(out: &mut String, m: &ResourceManifest, group_name: &str) {
    let group = match find_builder_group(m, group_name) {
        Some(g) => g,
        None => return,
    };
    let fields = group_fields(m, group_name);
    if fields.is_empty() {
        return;
    }

    let sdk_crate_mod = sdk_crate_to_mod(&m.resource.sdk_crate);
    let builder_var = safe_ident(group_name);
    let setter = snake_case(&group.setter);
    let type_name = &group.type_name;

    let _ = writeln!(out, "    let mut {builder_var}_builder = {sdk_crate_mod}::types::{type_name}::builder();");

    for (name, field) in &fields {
        let sdk_param = field.sdk_field.as_deref().unwrap_or(name);
        let field_setter = snake_case(sdk_param);
        let var = safe_ident(name);
        let has_default = field.default.is_some();

        match field.field_type.as_str() {
            "StringArray" => {
                if field.required {
                    // Required StringArray: use set_* with Some(vec)
                    let _ = writeln!(out, "    {builder_var}_builder = {builder_var}_builder.set_{field_setter}(Some({var}));");
                } else if has_default {
                    let _ = writeln!(out, "    {builder_var}_builder = {builder_var}_builder.set_{field_setter}(Some({var}));");
                } else {
                    let _ = writeln!(out, "    {builder_var}_builder = {builder_var}_builder.set_{field_setter}({var});");
                }
            }
            "String" => {
                if field.required || has_default {
                    let _ = writeln!(out, "    {builder_var}_builder = {builder_var}_builder.{field_setter}(&{var});");
                } else {
                    let _ = writeln!(out, "    if let Some(ref v) = {var} {{");
                    let _ = writeln!(out, "        {builder_var}_builder = {builder_var}_builder.{field_setter}(v);");
                    let _ = writeln!(out, "    }}");
                }
            }
            "Integer" | "Integer_u32" | "Integer_u64" => {
                if field.required || has_default {
                    let _ = writeln!(out, "    {builder_var}_builder = {builder_var}_builder.{field_setter}({var} as i32);");
                } else {
                    let _ = writeln!(out, "    if let Some(v) = {var} {{");
                    let _ = writeln!(out, "        {builder_var}_builder = {builder_var}_builder.{field_setter}(v as i32);");
                    let _ = writeln!(out, "    }}");
                }
            }
            "Float" => {
                if field.required || has_default {
                    let _ = writeln!(out, "    {builder_var}_builder = {builder_var}_builder.{field_setter}({var});");
                } else {
                    let _ = writeln!(out, "    if let Some(v) = {var} {{");
                    let _ = writeln!(out, "        {builder_var}_builder = {builder_var}_builder.{field_setter}(v);");
                    let _ = writeln!(out, "    }}");
                }
            }
            "Bool" => {
                if field.required || has_default {
                    let _ = writeln!(out, "    {builder_var}_builder = {builder_var}_builder.{field_setter}({var});");
                } else {
                    let _ = writeln!(out, "    if let Some(v) = {var} {{");
                    let _ = writeln!(out, "        {builder_var}_builder = {builder_var}_builder.{field_setter}(v);");
                    let _ = writeln!(out, "    }}");
                }
            }
            _ => {
                let _ = writeln!(out, "    // TODO: builder group field {name} (type: {})", field.field_type);
            }
        }
    }

    let _ = writeln!(out, "    let {builder_var} = {builder_var}_builder.build();");
    let _ = writeln!(out, "    req = req.{setter}({builder_var});");
    let _ = writeln!(out);
}

/// Emit code to build a nested builder type from grouped fields on update (all optional).
fn write_builder_group_update(out: &mut String, m: &ResourceManifest, group_name: &str) {
    let group = match find_builder_group(m, group_name) {
        Some(g) => g,
        None => return,
    };
    // Only include fields that are updatable (not required unless marked updatable)
    let fields: Vec<_> = group_fields(m, group_name).into_iter()
        .filter(|(_, f)| !f.output_only && !f.skip && !f.skip_update && (!f.required || f.updatable))
        .collect();
    if fields.is_empty() {
        return;
    }

    let sdk_crate_mod = sdk_crate_to_mod(&m.resource.sdk_crate);
    let builder_var = safe_ident(group_name);
    let setter = snake_case(&group.setter);
    let type_name = &group.type_name;

    let _ = writeln!(out, "    let mut {builder_var}_builder = {sdk_crate_mod}::types::{type_name}::builder();");

    for (name, field) in &fields {
        let sdk_param = field.sdk_field.as_deref().unwrap_or(name);
        let field_setter = snake_case(sdk_param);
        let var = safe_ident(name);

        match field.field_type.as_str() {
            "StringArray" => {
                let _ = writeln!(out, "    if let Some(ref arr) = {var} {{");
                let _ = writeln!(out, "        {builder_var}_builder = {builder_var}_builder.set_{field_setter}(Some(arr.clone()));");
                let _ = writeln!(out, "    }}");
            }
            "String" => {
                let _ = writeln!(out, "    if let Some(v) = {var} {{");
                let _ = writeln!(out, "        {builder_var}_builder = {builder_var}_builder.{field_setter}(v);");
                let _ = writeln!(out, "    }}");
            }
            "Integer" | "Integer_u32" | "Integer_u64" => {
                let _ = writeln!(out, "    if let Some(v) = {var} {{");
                let _ = writeln!(out, "        {builder_var}_builder = {builder_var}_builder.{field_setter}(v as i32);");
                let _ = writeln!(out, "    }}");
            }
            "Float" => {
                let _ = writeln!(out, "    if let Some(v) = {var} {{");
                let _ = writeln!(out, "        {builder_var}_builder = {builder_var}_builder.{field_setter}(v);");
                let _ = writeln!(out, "    }}");
            }
            "Bool" => {
                let _ = writeln!(out, "    if let Some(v) = {var} {{");
                let _ = writeln!(out, "        {builder_var}_builder = {builder_var}_builder.{field_setter}(v);");
                let _ = writeln!(out, "    }}");
            }
            _ => {
                let _ = writeln!(out, "    // TODO: builder group update field {name}");
            }
        }
    }

    let _ = writeln!(out, "    req = req.{setter}({builder_var}_builder.build());");
    let _ = writeln!(out);
}

// ── Read function ───────────────────────────────────────────────────────────

fn write_read_fn(out: &mut String, m: &ResourceManifest) {
    let fn_name = crud_fn_name("read", &m.resource.type_path);
    let is_gcp = m.resource.provider == "gcp";

    let _ = writeln!(out, "pub(super) async fn {fn_name}(");
    let _ = writeln!(out, "    &self,");
    let _ = writeln!(out, "    provider_id: &str,");
    let _ = writeln!(out, ") -> Result<ResourceOutput, ProviderError> {{");

    if is_gcp {
        // GCP needs provider_id parsing (zone/region extraction)
        write_provider_id_parsing(out, m);
        write_gcp_read_body(out, m);
    } else {
        // AWS uses provider_id directly
        write_aws_read_body(out, m);
    }

    let _ = writeln!(out, "}}");
    let _ = writeln!(out);
}

fn write_gcp_read_body(out: &mut String, m: &ResourceManifest) {
    let model_var = safe_ident(&snake_case(&m.resource.sdk_model));
    let client_accessor = m.resource.client_accessor.clone().unwrap_or_else(|| snake_case(&m.resource.sdk_client));
    let read_method = &m.crud.read;
    let model_name = &m.resource.sdk_model;
    let uses_resource_name = m.resource.api_style == ApiStyle::ResourceName
        || m.resource.api_style == ApiStyle::DirectModel;

    let _ = writeln!(out, "    let {model_var} = self");
    let _ = writeln!(out, "        .{client_accessor}().await?");
    let _ = writeln!(out, "        .{read_method}()");

    if uses_resource_name {
        // Resource-name / direct model style: set_name (or override) with the full resource path
        let name_setter = m.resource.resource_name_param.as_deref().unwrap_or("set_name");
        let _ = writeln!(out, "        .{name_setter}(provider_id)");
    } else {
        // Compute style: set_project + scope + resource name
        let _ = writeln!(out, "        .set_project(&self.project_id)");
        match m.resource.scope {
            Scope::Zonal => {
                let _ = writeln!(out, "        .set_zone(&zone)");
            }
            Scope::Regional => {
                let _ = writeln!(out, "        .set_region(&region)");
            }
            _ => {}
        }
        let resource_setter = m.resource.resource_id_param.clone()
            .unwrap_or_else(|| format!("set_{}", snake_case(&m.resource.sdk_model)));
        let _ = writeln!(out, "        .{resource_setter}(&name)");
    }

    let _ = writeln!(out, "        .send()");
    let _ = writeln!(out, "        .await");
    let _ = writeln!(
        out,
        "        .map_err(|e| super::classify_gcp_error(\"Get{model_name}\", e))?;"
    );
    let _ = writeln!(out);

    // Build state JSON
    write_state_json(out, m, &model_var);

    // Build outputs
    let _ = writeln!(out, "    let mut outputs = HashMap::new();");
    if let Some(ref output_field) = m.resource.output_field {
        // Custom output field specified
        let rf = raw_field(output_field);
        let _ = writeln!(
            out,
            "    outputs.insert(\"{output_field}\".into(), serde_json::json!(&{model_var}.{rf}));"
        );
    } else if uses_resource_name {
        let _ = writeln!(
            out,
            "    outputs.insert(\"name\".into(), serde_json::json!(&{model_var}.name));"
        );
    } else {
        let _ = writeln!(
            out,
            "    outputs.insert(\"self_link\".into(), serde_json::json!({model_var}.self_link.as_deref().unwrap_or(\"\")));"
        );
    }
    let _ = writeln!(out);

    let _ = writeln!(out, "    Ok(ResourceOutput {{");
    let _ = writeln!(out, "        provider_id: provider_id.to_string(),");
    let _ = writeln!(out, "        state,");
    let _ = writeln!(out, "        outputs,");
    let _ = writeln!(out, "    }})");
}

fn write_aws_read_body(out: &mut String, m: &ResourceManifest) {
    let client_field = m.resource.aws_client_field.as_deref().unwrap_or("client");
    let read_method = snake_case(&m.crud.read);
    let read_style = m.resource.aws_read_style.clone().unwrap_or(AwsReadStyle::GetSingle);
    let id_param = m.resource.aws_id_param.as_deref().unwrap_or("name");
    let id_setter = snake_case(id_param);
    let model_name = &m.resource.sdk_model;
    let has_composite = !m.resource.aws_composite_id.is_empty();

    // For composite IDs, parse provider_id into parts
    if has_composite {
        write_composite_id_parsing(out, &m.resource.aws_composite_id);
    }

    // Check if any non-skipped field actually references `resource.*()` in the state JSON.
    // If not, skip emitting the `resource` binding to avoid unused-variable warnings.
    let is_attrs = matches!(read_style, AwsReadStyle::GetAttributes);
    let needs_resource_binding = m.fields.iter().any(|(name, field)| {
        if field.output_only || field.skip_read { return false; }
        // ConfigName: "name" field uses provider_id, not resource
        if name == "name" && matches!(m.resource.aws_id_source, Some(AwsIdSource::ConfigName)) { return false; }
        // GetAttributes fields use attrs, not resource
        if is_attrs { return false; }
        true
    });

    // Make API call — use aws_read_id_param if set (e.g., log_group_name_prefix)
    let read_id_setter = m.resource.aws_read_id_param.as_deref()
        .map(|p| snake_case(p))
        .unwrap_or_else(|| id_setter.clone());
    // HeadCheck and GetSingle-without-accessor only reference `result` via `let resource = &result;`,
    // which is skipped when !needs_resource_binding — so prefix `result` with `_` when unused.
    let result_is_used = match read_style {
        AwsReadStyle::DescribeList | AwsReadStyle::GetAttributes => true,
        AwsReadStyle::GetSingle => m.resource.aws_response_accessor.is_some() || needs_resource_binding,
        AwsReadStyle::HeadCheck => needs_resource_binding,
    };
    let result_var = if result_is_used { "result" } else { "_result" };
    let _ = writeln!(out, "    let {result_var} = self.{client_field}");
    let _ = writeln!(out, "        .{read_method}()");
    if has_composite {
        write_composite_id_setters(out, &m.resource.aws_composite_id);
    } else {
        let _ = writeln!(out, "        .{read_id_setter}(provider_id)");
    }
    // For GetAttributes style, request all attributes
    if let (AwsReadStyle::GetAttributes, Some(attr_type)) = (&read_style, &m.resource.aws_attr_name_type) {
        let sdk_crate_mod = sdk_crate_to_mod(&m.resource.sdk_crate);
        let _ = writeln!(out, "        .attribute_names({sdk_crate_mod}::types::{attr_type}::All)");
    }
    let _ = writeln!(out, "        .send()");
    let _ = writeln!(out, "        .await");
    let _ = writeln!(
        out,
        "        .map_err(|e| ProviderError::ApiError(format!(\"{read_method}: {{e}}\")))?;"
    );
    let _ = writeln!(out);

    // Extract resource from response
    match read_style {
        AwsReadStyle::DescribeList => {
            let default_list = format!("{}s", snake_case(model_name));
            let list_accessor = m.resource.aws_list_accessor.as_deref()
                .unwrap_or(&default_list);
            let _ = writeln!(out, "    let resource = result");
            let _ = writeln!(out, "        .{list_accessor}()");
            let _ = writeln!(out, "        .first()");
            let _ = writeln!(out, "        .ok_or_else(|| ProviderError::NotFound(format!(\"{model_name} {{provider_id}}\")))?;");
        }
        AwsReadStyle::GetSingle => {
            if let Some(ref accessor) = m.resource.aws_response_accessor {
                let _ = writeln!(out, "    let resource = result");
                let _ = writeln!(out, "        .{accessor}()");
                let _ = writeln!(out, "        .ok_or_else(|| ProviderError::NotFound(format!(\"{model_name} {{provider_id}}\")))?;");
            } else if needs_resource_binding {
                let _ = writeln!(out, "    let resource = &result;");
            }
        }
        AwsReadStyle::GetAttributes => {
            // GetAttributes: resource access goes through attrs HashMap, no need for resource binding
        }
        AwsReadStyle::HeadCheck => {
            if needs_resource_binding {
                let _ = writeln!(out, "    let resource = &result;");
            }
        }
    }
    let _ = writeln!(out);

    // For GetAttributes, extract the attribute map
    if is_attrs {
        let _ = writeln!(out, "    let attrs = result.attributes().cloned().unwrap_or_default();");
        let _ = writeln!(out);
    }

    // Build state JSON
    let sections = group_fields_by_section(&m.fields);
    let _ = writeln!(out, "    let state = serde_json::json!({{");
    for (section_name, fields) in &sections {
        let _ = writeln!(out, "        \"{section_name}\": {{");
        for (field_name, field) in fields {
            if field.output_only || field.skip_read {
                continue;
            }
            let sdk_field = field.sdk_read_field.as_deref()
                .or(field.sdk_field.as_deref())
                .unwrap_or(field_name);
            let accessor = snake_case(sdk_field);

            // For config_name resources, the "name" field is the provider_id itself —
            // don't try to read it from the response object.
            let expr = if let Some(ref custom_expr) = field.read_expression {
                custom_expr.clone()
            } else if field_name == &"name" && matches!(m.resource.aws_id_source, Some(AwsIdSource::ConfigName)) {
                "provider_id".to_string()
            } else if is_attrs {
                // GetAttributes: look up from attribute HashMap
                // For "name" field with ResponseField id_source, extract from provider_id
                if field_name == &"name" && matches!(m.resource.aws_id_source, Some(AwsIdSource::ResponseField)) {
                    // Name is typically extracted from the ARN/URL-based provider_id
                    "provider_id.rsplit(':').next().unwrap_or(\"\")".to_string()
                } else {
                    let attr_key = field.aws_attr_key.as_deref()
                        .map(String::from)
                        .unwrap_or_else(|| capitalize(&accessor));
                    // Build the key expression — use SDK enum variant if attr_name_type is set
                    let key_expr = if let Some(ref attr_name_type) = m.resource.aws_attr_name_type {
                        let sdk_crate_mod = sdk_crate_to_mod(&m.resource.sdk_crate);
                        format!("&{sdk_crate_mod}::types::{attr_name_type}::{attr_key}")
                    } else {
                        format!("\"{attr_key}\"")
                    };
                    match field.field_type.as_str() {
                        "String" => format!("attrs.get({key_expr}).map(|v| v.as_str()).unwrap_or(\"\")"),
                        "Bool" => format!("attrs.get({key_expr}).map(|v| v == \"true\").unwrap_or(false)"),
                        "Integer" | "Integer_u32" | "Integer_u64" => format!("attrs.get({key_expr}).and_then(|v| v.parse::<i64>().ok()).unwrap_or(0)"),
                        _ => format!("attrs.get({key_expr}).map(|v| v.as_str()).unwrap_or(\"\")"),
                    }
                }
            } else if let Some(ref group_name) = field.aws_builder_group {
                // Builder group field: access through the group's read accessor
                // e.g., resource.scaling_config().and_then(|c| c.desired_size()).unwrap_or(2)
                let group_accessor = find_builder_group(m, group_name)
                    .map(|g| snake_case(g.read_accessor.as_deref().unwrap_or(&g.setter)))
                    .unwrap_or_else(|| snake_case(group_name));
                match field.field_type.as_str() {
                    "Bool" => format!("resource.{group_accessor}().map(|c| c.{accessor}()).unwrap_or(false)"),
                    "String" => format!("resource.{group_accessor}().and_then(|c| c.{accessor}()).unwrap_or(\"\")"),
                    "Integer" | "Integer_u32" | "Integer_u64" => format!("resource.{group_accessor}().and_then(|c| c.{accessor}()).unwrap_or(0)"),
                    "Float" => format!("resource.{group_accessor}().and_then(|c| c.{accessor}()).unwrap_or(0.0)"),
                    "StringArray" => format!("resource.{group_accessor}().map(|c| c.{accessor}()).unwrap_or_default()"),
                    _ => format!("resource.{group_accessor}().map(|c| c.{accessor}())"),
                }
            } else if let (Some(wrapper), Some(_wrapper_type)) = (&field.aws_builder_wrapper, &field.aws_builder_type) {
                // Builder-wrapped field: access through nested container
                // e.g., resource.image_scanning_configuration().map(|c| c.scan_on_push()).unwrap_or(false)
                let wrapper_accessor = snake_case(wrapper);
                match field.field_type.as_str() {
                    "Bool" => format!("resource.{wrapper_accessor}().map(|c| c.{accessor}()).unwrap_or(false)"),
                    "String" => format!("resource.{wrapper_accessor}().and_then(|c| c.{accessor}()).unwrap_or(\"\")"),
                    "Integer" | "Integer_u32" | "Integer_u64" => format!("resource.{wrapper_accessor}().and_then(|c| c.{accessor}()).unwrap_or(0)"),
                    _ => format!("resource.{wrapper_accessor}().map(|c| c.{accessor}())"),
                }
            } else if field.aws_enum {
                // SDK enum type — convert to string via .as_str()
                if field.sdk_non_optional {
                    format!("resource.{accessor}().as_str()")
                } else {
                    format!("resource.{accessor}().map(|r| r.as_str()).unwrap_or(\"\")")
                }
            } else if field.sdk_non_optional {
                // Non-optional SDK field — accessor returns T directly
                format!("resource.{accessor}()")
            } else {
                // Standard typed accessor pattern (returns Option<T>)
                match field.field_type.as_str() {
                    "String" => format!("resource.{accessor}().unwrap_or(\"\")"),
                    "Bool" => format!("resource.{accessor}().unwrap_or(false)"),
                    "Integer" | "Integer_u32" | "Integer_u64" => format!("resource.{accessor}().unwrap_or(0)"),
                    "Float" => format!("resource.{accessor}().unwrap_or(0.0)"),
                    "StringArray" => format!("resource.{accessor}()"),
                    _ => format!("resource.{accessor}()"),
                }
            };
            let _ = writeln!(out, "            \"{field_name}\": {expr},");
        }
        let _ = writeln!(out, "        }},");
    }
    let _ = writeln!(out, "    }});");
    let _ = writeln!(out);

    // Build outputs
    let _ = writeln!(out, "    let mut outputs = HashMap::new();");
    for output in &m.resource.aws_outputs {
        let _ = writeln!(
            out,
            "    outputs.insert(\"{}\".into(), serde_json::json!({}));",
            output.name, output.expression
        );
    }
    let _ = writeln!(out);

    let _ = writeln!(out, "    Ok(ResourceOutput {{");
    let _ = writeln!(out, "        provider_id: provider_id.to_string(),");
    let _ = writeln!(out, "        state,");
    let _ = writeln!(out, "        outputs,");
    let _ = writeln!(out, "    }})");
}

// ── Update function ─────────────────────────────────────────────────────────

fn write_update_fn(out: &mut String, m: &ResourceManifest) {
    let fn_name = crud_fn_name("update", &m.resource.type_path);

    let has_update = m.crud.update.is_some();
    // Replace-only resources: update method exists but is never dispatched from mod.rs
    let is_replace_only = !has_update || (m.resource.provider != "gcp" && !m.resource.aws_updatable);
    if is_replace_only {
        let _ = writeln!(out, "#[allow(dead_code)]");
    }
    let pid_prefix = if has_update { "" } else { "_" };
    // AWS update: config is unused when no fields are updatable (all required + not marked updatable)
    let has_any_update_fields = m.fields.iter().any(|(_, f)| {
        !f.output_only && !f.skip && (!f.required || f.updatable)
    });
    let cfg_prefix = if has_update && (m.resource.provider == "gcp" || has_any_update_fields) { "" } else { "_" };
    let _ = writeln!(out, "pub(super) async fn {fn_name}(");
    let _ = writeln!(out, "    &self,");
    let _ = writeln!(out, "    {pid_prefix}provider_id: &str,");
    let _ = writeln!(out, "    {cfg_prefix}config: &serde_json::Value,");
    let _ = writeln!(out, ") -> Result<ResourceOutput, ProviderError> {{");

    if has_update {
        if m.resource.provider == "gcp" {
            write_provider_id_parsing(out, m);
            write_gcp_update_body(out, m);
        } else {
            // AWS uses provider_id directly
            write_aws_update_body(out, m);
        }
    } else {
        let _ = writeln!(
            out,
            "    Err(ProviderError::RequiresReplacement(\"resource does not support in-place update\".into()))"
        );
    }

    let _ = writeln!(out, "}}");
    let _ = writeln!(out);
}

fn write_gcp_update_body(out: &mut String, m: &ResourceManifest) {
    let update_method = m.crud.update.as_deref().unwrap_or("patch");
    let sdk_crate_mod = sdk_crate_to_mod(&m.resource.sdk_crate);
    let client_accessor = m.resource.client_accessor.clone().unwrap_or_else(|| snake_case(&m.resource.sdk_client));
    let model_name = &m.resource.sdk_model;
    let model = &m.resource.sdk_model;
    // Re-extract fields from config for update
    let _ = writeln!(out, "    // Extract fields from config");
    for (name, field) in &m.fields {
        if field.output_only || field.skip || name == "labels" || name == "name" || name == "zone" || name == "region" {
            continue;
        }
        write_field_extraction(out, name, field, &sdk_crate_mod);
    }

    if m.fields.contains_key("labels") {
        if m.resource.raw_labels {
            let _ = writeln!(out, "    let labels: HashMap<String, String> = config");
            let _ = writeln!(out, "        .pointer(\"/identity/labels\")");
            let _ = writeln!(out, "        .and_then(|v| v.as_object())");
            let _ = writeln!(out, "        .map(|m| {{");
            let _ = writeln!(out, "            m.iter()");
            let _ = writeln!(out, "                .filter_map(|(k, v)| v.as_str().map(|s| (k.clone(), s.to_string())))");
            let _ = writeln!(out, "                .collect()");
            let _ = writeln!(out, "        }})");
            let _ = writeln!(out, "        .unwrap_or_default();");
        } else {
            let _ = writeln!(out, "    let labels = super::extract_labels(config);");
        }
    }
    let _ = writeln!(out);

    // DirectModel update wraps UpdateXxxRequest with set_xxx(Model) + set_update_mask,
    // so it uses the same model-building path as ResourceName
    {
        let _ = writeln!(
            out,
            "    let mut model = {sdk_crate_mod}::model::{model}::default();"
        );

        let uses_resource_name = m.resource.api_style == ApiStyle::ResourceName
            || m.resource.api_style == ApiStyle::DirectModel;

        // For resource_name/direct_model style, set the name on the model
        if uses_resource_name {
            let _ = writeln!(out, "    model = model.set_name(provider_id);");
        }

        // Set updatable fields
        for (name, field) in &m.fields {
            if field.output_only || field.skip || name == "labels" || name == "name" || name == "zone" || name == "region" {
                continue;
            }
            let sdk_field = field.sdk_field.as_deref().unwrap_or(name);
            let setter = format!("set_{sdk_field}");
            write_model_setter(out, name, &setter, field, &sdk_crate_mod);
        }

        if m.fields.contains_key("labels") {
            let _ = writeln!(out, "    model = model.set_labels(labels);");
        }
        let _ = writeln!(out);

        let _ = writeln!(out, "    self.{client_accessor}().await?");
        let _ = writeln!(out, "        .{update_method}()");

        if uses_resource_name {
            // Resource-name style: set the model body (which contains the name)
            let body_setter = m.resource.resource_body_setter.as_deref().unwrap_or("set_body");
            let _ = writeln!(out, "        .{body_setter}(model)");
            if m.resource.has_update_mask {
                let _ = writeln!(out, "        .set_update_mask(google_cloud_wkt::FieldMask::default())");
            }
        } else {
            // Compute style: set_project + scope + resource name + body
            let _ = writeln!(out, "        .set_project(&self.project_id)");
            match m.resource.scope {
                Scope::Zonal => {
                    let _ = writeln!(out, "        .set_zone(&zone)");
                }
                Scope::Regional => {
                    let _ = writeln!(out, "        .set_region(&region)");
                }
                _ => {}
            }
            let resource_setter = m.resource.resource_id_param.clone()
                .unwrap_or_else(|| format!("set_{}", snake_case(&m.resource.sdk_model)));
            let _ = writeln!(out, "        .{resource_setter}(&name)");
            let _ = writeln!(out, "        .set_body(model)");
        }
    }

    if m.resource.lro_update {
        let _ = writeln!(out, "        .poller()");
        let _ = writeln!(out, "        .until_done()");
        let _ = writeln!(out, "        .await");
    } else {
        let _ = writeln!(out, "        .send()");
        let _ = writeln!(out, "        .await");
    }
    let _ = writeln!(
        out,
        "        .map_err(|e| super::classify_gcp_error(\"{update_method} {model_name}\", e))?;"
    );
    let _ = writeln!(out);

    let read_fn = crud_fn_name("read", &m.resource.type_path);
    let _ = writeln!(out, "    self.{read_fn}(provider_id).await");
}

fn write_aws_update_body(out: &mut String, m: &ResourceManifest) {
    let client_field = m.resource.aws_client_field.as_deref().unwrap_or("client");
    let update_method = snake_case(m.crud.update.as_deref().unwrap_or("update"));
    let id_param = m.resource.aws_id_param.as_deref().unwrap_or("name");
    let id_setter = snake_case(id_param);
    let has_composite = !m.resource.aws_composite_id.is_empty();

    // For composite IDs, parse provider_id into parts
    if has_composite {
        write_composite_id_parsing(out, &m.resource.aws_composite_id);
    }

    // A field is included in the update path if it's not output-only, not skipped,
    // and either optional (not required) or explicitly marked `updatable`.
    let is_update_field = |f: &FieldDef| -> bool {
        !f.output_only && !f.skip && !f.skip_update && (!f.required || f.updatable)
    };

    // Extract fields from config using optional_* (update only sets what's present)
    for (name, field) in &m.fields {
        if !is_update_field(field) {
            continue;
        }
        let section = &field.section;
        let path = format!("/{section}/{name}");
        let var = safe_ident(name);

        match field.field_type.as_str() {
            "String" => {
                let _ = writeln!(out, "    let {var} = config.optional_str(\"{path}\");");
            }
            "Integer" | "Integer_u32" | "Integer_u64" => {
                let _ = writeln!(out, "    let {var} = config.optional_i64(\"{path}\");");
            }
            "Float" => {
                let _ = writeln!(out, "    let {var} = config.optional_f64(\"{path}\");");
            }
            "Bool" => {
                let _ = writeln!(out, "    let {var} = config.optional_bool(\"{path}\");");
            }
            "StringArray" => {
                let _ = writeln!(
                    out,
                    "    let {var}: Option<Vec<String>> = config.optional_array(\"{path}\").map(|arr| arr.iter().filter_map(|v| v.as_str().map(String::from)).collect());"
                );
            }
            _ => {}
        }
    }
    let _ = writeln!(out);

    // Check if we need `mut` for the update builder
    let has_updatable_fields = m.fields.iter().any(|(_, f)| is_update_field(f));
    let has_update_builder_groups = m.fields.iter().any(|(_, f)| is_update_field(f) && f.aws_builder_group.is_some());
    let update_let = if has_updatable_fields || has_update_builder_groups { "let mut req" } else { "let req" };
    let _ = writeln!(out, "    {update_let} = self.{client_field}");
    let _ = writeln!(out, "        .{update_method}()");
    if has_composite {
        write_composite_id_setters(out, &m.resource.aws_composite_id);
    } else {
        let _ = writeln!(out, "        .{id_setter}(provider_id)");
    }
    let _ = writeln!(out, "    ;");
    let _ = writeln!(out);

    // Set updatable fields conditionally (skip builder group fields — handled below)
    for (name, field) in &m.fields {
        if !is_update_field(field) || field.aws_builder_group.is_some() {
            continue;
        }
        let sdk_param = field.sdk_field.as_deref().unwrap_or(name);
        let setter = snake_case(sdk_param);
        let var = safe_ident(name);

        // Fields with aws_attr_key use .attributes(key, value) instead of typed setters
        // Add .into() only when aws_attr_name_type is set (SDK uses a typed enum like QueueAttributeName)
        if let Some(ref attr_key) = field.aws_attr_key {
            let into_suffix = if m.resource.aws_attr_name_type.is_some() { ".into()" } else { "" };
            match field.field_type.as_str() {
                "Bool" => {
                    let _ = writeln!(out, "    if let Some(true) = {var} {{");
                    let _ = writeln!(out, "        req = req.attributes(\"{attr_key}\"{into_suffix}, \"true\");");
                    let _ = writeln!(out, "    }}");
                }
                "String" => {
                    let _ = writeln!(out, "    if let Some(v) = {var} {{");
                    let _ = writeln!(out, "        req = req.attributes(\"{attr_key}\"{into_suffix}, v);");
                    let _ = writeln!(out, "    }}");
                }
                "Integer" | "Integer_u32" | "Integer_u64" => {
                    let _ = writeln!(out, "    if let Some(v) = {var} {{");
                    let _ = writeln!(out, "        req = req.attributes(\"{attr_key}\"{into_suffix}, v.to_string());");
                    let _ = writeln!(out, "    }}");
                }
                _ => {}
            }
            continue;
        }

        if field.aws_enum {
            // SDK enum type — convert from string via From<&str>
            let sdk_crate_mod = sdk_crate_to_mod(&m.resource.sdk_crate);
            let enum_type = field.aws_enum_type.as_deref()
                .map(String::from)
                .unwrap_or_else(|| capitalize(sdk_param));
            let _ = writeln!(out, "    if let Some(v) = {var} {{");
            let _ = writeln!(out, "        req = req.{setter}({sdk_crate_mod}::types::{enum_type}::from(v));");
            let _ = writeln!(out, "    }}");
            continue;
        }

        match field.field_type.as_str() {
            "String" => {
                let _ = writeln!(out, "    if let Some(v) = {var} {{");
                let _ = writeln!(out, "        req = req.{setter}(v);");
                let _ = writeln!(out, "    }}");
            }
            "Integer" | "Integer_u32" | "Integer_u64" => {
                let _ = writeln!(out, "    if let Some(v) = {var} {{");
                let _ = writeln!(out, "        req = req.{setter}(v as i32);");
                let _ = writeln!(out, "    }}");
            }
            "Float" => {
                let _ = writeln!(out, "    if let Some(v) = {var} {{");
                let _ = writeln!(out, "        req = req.{setter}(v);");
                let _ = writeln!(out, "    }}");
            }
            "Bool" => {
                let _ = writeln!(out, "    if let Some(v) = {var} {{");
                let _ = writeln!(out, "        req = req.{setter}(v);");
                let _ = writeln!(out, "    }}");
            }
            "StringArray" => {
                let _ = writeln!(out, "    if let Some(ref arr) = {var} {{");
                let _ = writeln!(out, "        for v in arr {{");
                let _ = writeln!(out, "            req = req.{setter}(v);");
                let _ = writeln!(out, "        }}");
                let _ = writeln!(out, "    }}");
            }
            _ => {
                let _ = writeln!(out, "    // TODO: set optional {name} via .{setter}()");
            }
        }
    }
    let _ = writeln!(out);

    for extra in &m.resource.aws_update_extra_setters {
        let _ = writeln!(out, "    req = req.{extra};");
    }

    // Build and set builder groups for update
    for group_name in all_builder_group_names(m) {
        // Only emit update builder group if it has updatable fields
        let group_has_update_fields = m.fields.iter().any(|(_, f)| {
            f.aws_builder_group.as_deref() == Some(&group_name) && is_update_field(f)
        });
        if group_has_update_fields {
            write_builder_group_update(out, m, &group_name);
        }
    }

    let _ = writeln!(out, "    req.send()");
    let _ = writeln!(out, "        .await");
    let _ = writeln!(
        out,
        "        .map_err(|e| ProviderError::ApiError(format!(\"{update_method}: {{e}}\")))?;"
    );
    let _ = writeln!(out);

    let read_fn = crud_fn_name("read", &m.resource.type_path);
    let _ = writeln!(out, "    self.{read_fn}(provider_id).await");
}

// ── Delete function ─────────────────────────────────────────────────────────

fn write_delete_fn(out: &mut String, m: &ResourceManifest) {
    let fn_name = crud_fn_name("delete", &m.resource.type_path);

    let _ = writeln!(out, "pub(super) async fn {fn_name}(");
    let _ = writeln!(out, "    &self,");
    let _ = writeln!(out, "    provider_id: &str,");
    let _ = writeln!(out, ") -> Result<(), ProviderError> {{");

    if m.crud.delete.is_none() {
        let _ = writeln!(out, "    Err(ProviderError::ApiError(\"delete not supported for this resource type\".into()))");
        let _ = writeln!(out, "}}");
        let _ = writeln!(out);
        return;
    }

    if m.resource.provider == "gcp" {
        write_provider_id_parsing(out, m);
        let client_accessor = m.resource.client_accessor.clone().unwrap_or_else(|| snake_case(&m.resource.sdk_client));
        let model_name = &m.resource.sdk_model;
        let delete_method = m.crud.delete.as_deref().unwrap();
        let uses_resource_name = m.resource.api_style == ApiStyle::ResourceName
            || m.resource.api_style == ApiStyle::DirectModel;

        let _ = writeln!(out, "    self.{client_accessor}().await?");
        let _ = writeln!(out, "        .{delete_method}()");

        if uses_resource_name {
            let name_setter = m.resource.resource_name_param.as_deref().unwrap_or("set_name");
            let _ = writeln!(out, "        .{name_setter}(provider_id)");
        } else {
            let _ = writeln!(out, "        .set_project(&self.project_id)");
            match m.resource.scope {
                Scope::Zonal => {
                    let _ = writeln!(out, "        .set_zone(&zone)");
                }
                Scope::Regional => {
                    let _ = writeln!(out, "        .set_region(&region)");
                }
                _ => {}
            }
            let resource_setter = m.resource.resource_id_param.clone()
                .unwrap_or_else(|| format!("set_{}", snake_case(&m.resource.sdk_model)));
            let _ = writeln!(out, "        .{resource_setter}(&name)");
        }

        if m.resource.lro_delete {
            let _ = writeln!(out, "        .poller()");
            let _ = writeln!(out, "        .until_done()");
            let _ = writeln!(out, "        .await");
        } else {
            let _ = writeln!(out, "        .send()");
            let _ = writeln!(out, "        .await");
        }
        let _ = writeln!(
            out,
            "        .map_err(|e| super::classify_gcp_error(\"Delete{model_name}\", e))?;"
        );
    } else {
        let client_field = m.resource.aws_client_field.as_deref().unwrap_or("client");
        let delete_method = snake_case(m.crud.delete.as_deref().unwrap());
        let id_param = m.resource.aws_id_param.as_deref().unwrap_or("name");
        let has_composite = !m.resource.aws_composite_id.is_empty();

        // For composite IDs, parse provider_id into parts
        if has_composite {
            write_composite_id_parsing(out, &m.resource.aws_composite_id);
        }

        let _ = writeln!(out, "    self.{client_field}");
        let _ = writeln!(out, "        .{delete_method}()");
        if has_composite {
            write_composite_id_setters(out, &m.resource.aws_composite_id);
        } else {
            let delete_id_setter = m.resource.aws_delete_id_param.as_deref()
                .map(|p| snake_case(p))
                .unwrap_or_else(|| snake_case(id_param));
            let _ = writeln!(out, "        .{delete_id_setter}(provider_id)");
        }
        for extra in &m.resource.aws_delete_extra_setters {
            let _ = writeln!(out, "        .{extra}");
        }
        let _ = writeln!(out, "        .send()");
        let _ = writeln!(out, "        .await");
        let _ = writeln!(
            out,
            "        .map_err(|e| ProviderError::ApiError(format!(\"{delete_method}: {{e}}\")))?;"
        );
    }

    let _ = writeln!(out, "    Ok(())");
    let _ = writeln!(out, "}}");
    let _ = writeln!(out);
}

// ── Diff rules ──────────────────────────────────────────────────────────────

fn write_diff_rules(out: &mut String, m: &ResourceManifest) {
    if m.replacement_fields.is_empty() {
        return;
    }

    let _ = writeln!(out, "// Diff: fields that force replacement");
    let _ = writeln!(
        out,
        "pub(super) fn {}_forces_replacement(path: &str) -> bool {{",
        m.resource.type_path.replace('.', "_").to_lowercase()
    );
    let _ = writeln!(out, "    matches!(path,");

    for (i, field) in m.replacement_fields.iter().enumerate() {
        // Find section for this field
        let section = m
            .fields
            .get(field)
            .map(|f| f.section.as_str())
            .unwrap_or("identity");
        let trailing = if i < m.replacement_fields.len() - 1 {
            " |"
        } else {
            ""
        };
        let _ = writeln!(out, "        \"{section}.{field}\"{trailing}");
    }

    let _ = writeln!(out, "    )");
    let _ = writeln!(out, "}}");
}

// ── Helpers ─────────────────────────────────────────────────────────────────

/// Convert SDK crate name to Rust module path.
/// "google-cloud-compute-v1" → "google_cloud_compute_v1"
/// "google-cloud-secretmanager-v1" → "google_cloud_secretmanager_v1"
fn sdk_crate_to_mod(sdk_crate: &str) -> String {
    sdk_crate.replace('-', "_")
}

/// Convert type_path to snake_case function suffix.
/// "logs.LogGroup" → "logs_log_group", "lambda.Function" → "lambda_function"
fn type_path_to_snake(type_path: &str) -> String {
    type_path
        .split('.')
        .map(|part| snake_case(part))
        .collect::<Vec<_>>()
        .join("_")
}

fn schema_fn_name(type_path: &str) -> String {
    format!("{}_schema", type_path_to_snake(type_path))
}

fn crud_fn_name(operation: &str, type_path: &str) -> String {
    format!("{operation}_{}", type_path_to_snake(type_path))
}

/// Convert snake_case to PascalCase: "comparison_operator" → "ComparisonOperator"
fn capitalize(s: &str) -> String {
    s.split('_')
        .map(|part| {
            let mut chars = part.chars();
            match chars.next() {
                None => String::new(),
                Some(c) => c.to_uppercase().chain(chars).collect(),
            }
        })
        .collect()
}

fn field_type_expr(type_str: &str, variants: &[String]) -> String {
    match type_str {
        "String" => "crate::provider::FieldType::String".into(),
        "Bool" => "crate::provider::FieldType::Bool".into(),
        "Integer" | "Integer_u32" | "Integer_u64" => "crate::provider::FieldType::Integer".into(),
        "Float" => "crate::provider::FieldType::Float".into(),
        "StringArray" => "crate::provider::FieldType::Array(Box::new(crate::provider::FieldType::String))".into(),
        s if s.starts_with("Enum(") => {
            if variants.is_empty() {
                "crate::provider::FieldType::String /* TODO: Enum variants */".into()
            } else {
                let vs: Vec<String> = variants
                    .iter()
                    .map(|v| format!("\"{v}\".into()"))
                    .collect();
                format!(
                    "crate::provider::FieldType::Enum(vec![{}])",
                    vs.join(", ")
                )
            }
        }
        s if s.starts_with("Array(") => {
            let inner = &s[6..s.len() - 1];
            format!(
                "crate::provider::FieldType::Array(Box::new({}))",
                field_type_expr(inner, variants)
            )
        }
        "Ref" => "crate::provider::FieldType::Ref(\"*\".into())".into(),
        "Record" => "crate::provider::FieldType::Record(vec![])".into(),
        s if s.starts_with("Oneof(") || s.starts_with("Nested(") => "crate::provider::FieldType::Record(vec![])".into(),
        "Duration" | "Timestamp" | "Bytes" => "crate::provider::FieldType::String".into(),
        _ => format!("crate::provider::FieldType::String /* {type_str} */"),
    }
}

fn toml_to_json_literal(v: &toml::Value) -> String {
    match v {
        toml::Value::String(s) => {
            // Use Rust debug formatting which properly escapes " and \
            format!("{s:?}")
        }
        toml::Value::Integer(i) => format!("{i}"),
        toml::Value::Float(f) => format!("{f}"),
        toml::Value::Boolean(b) => format!("{b}"),
        _ => "null".into(),
    }
}

/// Group fields by section, preserving field order within each section.
fn group_fields_by_section(
    fields: &BTreeMap<String, FieldDef>,
) -> Vec<(String, Vec<(&String, &FieldDef)>)> {
    let mut sections: BTreeMap<String, Vec<(&String, &FieldDef)>> = BTreeMap::new();
    for (name, field) in fields {
        if field.skip {
            continue;
        }
        sections
            .entry(field.section.clone())
            .or_default()
            .push((name, field));
    }
    // Put "identity" first, then alphabetical
    let mut result = Vec::new();
    if let Some(identity) = sections.remove("identity") {
        result.push(("identity".into(), identity));
    }
    for (section, fields) in sections {
        result.push((section, fields));
    }
    result
}

fn write_field_extraction(out: &mut String, name: &str, field: &FieldDef, sdk_crate_mod: &str) {
    let section = &field.section;
    let path = format!("/{section}/{name}");
    let var = safe_ident(name);

    match field.field_type.as_str() {
        "String" => {
            if field.required {
                let _ = writeln!(
                    out,
                    "    let {var} = config.require_str(\"{path}\")?.to_string();"
                );
            } else if let Some(ref default) = field.default {
                let _ = writeln!(
                    out,
                    "    let {var} = config.str_or(\"{path}\", {}).to_string();",
                    toml_to_json_literal(default)
                );
            } else {
                let _ = writeln!(
                    out,
                    "    let {var} = config.optional_str(\"{path}\").map(String::from);"
                );
            }
        }
        "Bool" => {
            if field.required {
                let _ = writeln!(
                    out,
                    "    let {var} = config.require_bool(\"{path}\")?;"
                );
            } else if let Some(ref default) = field.default {
                let _ = writeln!(
                    out,
                    "    let {var} = config.bool_or(\"{path}\", {});",
                    toml_to_json_literal(default)
                );
            } else {
                let _ = writeln!(
                    out,
                    "    let {var} = config.optional_bool(\"{path}\");"
                );
            }
        }
        "Integer" | "Integer_u32" | "Integer_u64" => {
            if field.required {
                let _ = writeln!(
                    out,
                    "    let {var} = config.require_i64(\"{path}\")?;"
                );
            } else if let Some(ref default) = field.default {
                let _ = writeln!(
                    out,
                    "    let {var} = config.i64_or(\"{path}\", {});",
                    toml_to_json_literal(default)
                );
            } else {
                let _ = writeln!(
                    out,
                    "    let {var} = config.optional_i64(\"{path}\");"
                );
            }
        }
        "Float" => {
            if field.required {
                let _ = writeln!(
                    out,
                    "    let {var} = config.require_f64(\"{path}\")?;"
                );
            } else if let Some(ref default) = field.default {
                let _ = writeln!(
                    out,
                    "    let {var} = config.f64_or(\"{path}\", {});",
                    toml_to_json_literal(default)
                );
            } else {
                let _ = writeln!(
                    out,
                    "    let {var} = config.optional_f64(\"{path}\");"
                );
            }
        }
        "StringArray" => {
            // StringArray: extract as Vec<String> from JSON array
            if field.required {
                let _ = writeln!(
                    out,
                    "    let {var}: Vec<String> = config.optional_array(\"{path}\").map(|arr| arr.iter().filter_map(|v| v.as_str().map(String::from)).collect()).unwrap_or_default();"
                );
            } else {
                let _ = writeln!(
                    out,
                    "    let {var}: Option<Vec<String>> = config.optional_array(\"{path}\").map(|arr| arr.iter().filter_map(|v| v.as_str().map(String::from)).collect());"
                );
            }
        }
        s if s.starts_with("Enum(") => {
            // Enums are extracted as strings from config, then converted via from_name() in the setter
            if field.required {
                let _ = writeln!(
                    out,
                    "    let {var} = config.require_str(\"{path}\")?.to_string();"
                );
            } else {
                let _ = writeln!(
                    out,
                    "    let {var} = config.optional_str(\"{path}\").map(String::from);"
                );
            }
        }
        s if s.starts_with("Oneof(") => {
            // Oneof fields: extraction is done inline in write_model_setter
            // Nothing to extract here
        }
        _ => {
            // Nested, Array, Record, Duration, Timestamp — deserialize from config JSON
            if let Some(ref type_path) = field.sdk_type_path {
                let full_type = type_path.replace("crate::", &format!("{sdk_crate_mod}::"));
                let _ = writeln!(
                    out,
                    "    let {var} = config.pointer(\"{path}\").and_then(|v| serde_json::from_value::<{full_type}>(v.clone()).ok());"
                );
            } else {
                let _ = writeln!(
                    out,
                    "    // TODO: extract {var} ({}) from config[\"{path}\"] — type path unknown",
                    field.field_type
                );
            }
        }
    }
}

fn write_model_setter(out: &mut String, name: &str, setter: &str, field: &FieldDef, sdk_crate_mod: &str) {
    let var = safe_ident(name);
    match field.field_type.as_str() {
        "String" => {
            if field.required {
                let _ = writeln!(out, "    model = model.{setter}({var}.clone());");
            } else {
                let _ = writeln!(out, "    if let Some(v) = {var} {{");
                let _ = writeln!(out, "        model = model.{setter}(v);");
                let _ = writeln!(out, "    }}");
            }
        }
        "Bool" => {
            if field.required {
                let _ = writeln!(out, "    model = model.{setter}({var});");
            } else {
                let _ = writeln!(out, "    if let Some(v) = {var} {{");
                let _ = writeln!(out, "        model = model.{setter}(v);");
                let _ = writeln!(out, "    }}");
            }
        }
        "Integer" | "Integer_u32" | "Integer_u64" => {
            // Checked cast from extracted i64 to the SDK's expected integer type
            let cast_type = match field.field_type.as_str() {
                "Integer_u32" => "u32",
                "Integer_u64" => "u64",
                _ => "i32", // default: most SDK fields use i32
            };
            if field.required {
                let _ = writeln!(out, "    model = model.{setter}({cast_type}::try_from({var}).map_err(|_| ProviderError::InvalidConfig(format!(\"{name}: value {{}} out of range for {cast_type}\", {var})))?);");
            } else {
                let _ = writeln!(out, "    if let Some(v) = {var} {{");
                let _ = writeln!(out, "        model = model.{setter}({cast_type}::try_from(v).map_err(|_| ProviderError::InvalidConfig(format!(\"{name}: value {{v}} out of range for {cast_type}\")))?);");
                let _ = writeln!(out, "    }}");
            }
        }
        "Float" => {
            // f64→f32 is lossy but there's no TryFrom; `as f32` is acceptable
            // for the percentage/ratio values in GCP APIs
            if field.required {
                let _ = writeln!(out, "    model = model.{setter}({var} as f32);");
            } else {
                let _ = writeln!(out, "    if let Some(v) = {var} {{");
                let _ = writeln!(out, "        model = model.{setter}(v as f32);");
                let _ = writeln!(out, "    }}");
            }
        }
        "StringArray" => {
            // Vec<String> fields: iterate and call builder method for each element
            if field.required {
                let _ = writeln!(out, "    for v in &{var} {{");
                let _ = writeln!(out, "        model = model.{setter}(v.clone());");
                let _ = writeln!(out, "    }}");
            } else {
                let _ = writeln!(out, "    if let Some(ref arr) = {var} {{");
                let _ = writeln!(out, "        for v in arr {{");
                let _ = writeln!(out, "            model = model.{setter}(v.clone());");
                let _ = writeln!(out, "        }}");
                let _ = writeln!(out, "    }}");
            }
        }
        s if s.starts_with("Enum(") => {
            // Convert string to SDK enum via From<&str>
            if let Some(ref type_path) = field.sdk_type_path {
                let full_path = type_path.replace("crate::", &format!("{sdk_crate_mod}::"));
                if field.required {
                    // required field: var was extracted as String
                    let _ = writeln!(out, "    model = model.{setter}({full_path}::from({var}.as_str()));");
                } else {
                    // optional field: var was extracted as Option<String>
                    let _ = writeln!(out, "    if let Some(ref s) = {var} {{");
                    let _ = writeln!(out, "        model = model.{setter}({full_path}::from(s.as_str()));");
                    let _ = writeln!(out, "    }}");
                }
            } else {
                let _ = writeln!(out, "    // TODO: set {name} on model via .{setter}() — enum type path unknown");
            }
        }
        s if s.starts_with("Oneof(") => {
            // Proto oneof: check each variant's config path and call the variant-specific setter
            if field.oneof_variants.is_empty() {
                let _ = writeln!(out, "    // TODO: set {name} on model via .{setter}() — no oneof variants parsed");
                return;
            }
            write_oneof_setters(out, name, &field.section, &field.oneof_variants, sdk_crate_mod);
        }
        _ => {
            // Nested, Array, Record, Duration, Timestamp — already extracted as Option<T>
            if field.sdk_type_path.is_some() {
                let _ = writeln!(out, "    if let Some(v) = {var} {{");
                let _ = writeln!(out, "        model = model.{setter}(v);");
                let _ = writeln!(out, "    }}");
            } else {
                let _ = writeln!(out, "    // TODO: set {name} on model via .{setter}() — type path unknown");
            }
        }
    }
}

/// Write a setter call that chains on a request builder (for direct_model style).
/// Used in create/update where setters are called on the builder, not on a model variable.
fn write_request_setter(out: &mut String, name: &str, setter: &str, field: &FieldDef) {
    let var = safe_ident(name);
    match field.field_type.as_str() {
        "String" => {
            if field.required {
                let _ = writeln!(out, "        .{setter}({var}.clone())");
            } else {
                // Optional strings: wrap in set_or_clear with Option
                let _ = writeln!(out, "        .set_or_clear_{setter}({var})", setter = setter.strip_prefix("set_").unwrap_or(setter));
            }
        }
        "Bool" => {
            if field.required {
                let _ = writeln!(out, "        .{setter}({var})");
            } else {
                let _ = writeln!(out, "        // TODO: set optional bool {name} via .{setter}()");
            }
        }
        "Integer" | "Integer_u32" | "Integer_u64" => {
            let cast_type = match field.field_type.as_str() {
                "Integer_u32" => "u32",
                "Integer_u64" => "u64",
                _ => "i32",
            };
            if field.required {
                let _ = writeln!(out, "        .{setter}({var} as {cast_type})");
            } else {
                let _ = writeln!(out, "        // TODO: set optional {cast_type} {name} via .{setter}()");
            }
        }
        _ => {
            let _ = writeln!(out, "        // TODO: set {name} via .{setter}()");
        }
    }
}

/// Generate combined extraction+setter code for each oneof variant.
///
/// For a field "expiration" with variants [ExpireTime(Timestamp), Ttl(Duration)]:
/// ```ignore
/// if let Some(v) = config.optional_str("/config/expire_time") {
///     model = model.set_expire_time(google_cloud_wkt::Timestamp::try_from(v)
///         .map_err(|e| ProviderError::InvalidConfig(format!("expire_time: {e}")))?);
/// } else if let Some(v) = config.optional_str("/config/ttl") {
///     model = model.set_ttl(google_cloud_wkt::Duration::try_from(v)
///         .map_err(|e| ProviderError::InvalidConfig(format!("ttl: {e}")))?);
/// }
/// ```
fn write_oneof_setters(
    out: &mut String,
    _field_name: &str,
    section: &str,
    variants: &[crate::introspect::OneofVariant],
    sdk_crate_mod: &str,
) {
    use crate::introspect::OneofInnerType;

    let mut first = true;
    for variant in variants {
        let setter = &variant.setter;
        let var_snake = &variant.setter[4..]; // strip "set_" prefix for config path

        let if_keyword = if first { "if" } else { "} else if" };

        match &variant.inner_type {
            OneofInnerType::String => {
                first = false;
                let _ = writeln!(out, "    {if_keyword} let Some(v) = config.optional_str(\"/{section}/{var_snake}\") {{");
                let _ = writeln!(out, "        model = model.{setter}(v);");
            }
            OneofInnerType::Timestamp => {
                first = false;
                let _ = writeln!(out, "    {if_keyword} let Some(v) = config.optional_str(\"/{section}/{var_snake}\") {{");
                let _ = writeln!(out, "        model = model.{setter}(google_cloud_wkt::Timestamp::try_from(v)");
                let _ = writeln!(out, "            .map_err(|e| ProviderError::InvalidConfig(format!(\"{var_snake}: {{e}}\")))?);");
            }
            OneofInnerType::Duration => {
                first = false;
                let _ = writeln!(out, "    {if_keyword} let Some(v) = config.optional_str(\"/{section}/{var_snake}\") {{");
                let _ = writeln!(out, "        model = model.{setter}(google_cloud_wkt::Duration::try_from(v)");
                let _ = writeln!(out, "            .map_err(|e| ProviderError::InvalidConfig(format!(\"{var_snake}: {{e}}\")))?);");
            }
            OneofInnerType::Integer(int_type) => {
                first = false;
                let _ = writeln!(out, "    {if_keyword} let Some(v) = config.pointer(\"/{section}/{var_snake}\").and_then(|v| v.as_i64()) {{");
                let _ = writeln!(out, "        model = model.{setter}({int_type}::try_from(v)");
                let _ = writeln!(out, "            .map_err(|_| ProviderError::InvalidConfig(format!(\"{var_snake}: value {{v}} out of range for {int_type}\")))?);");
            }
            OneofInnerType::Float => {
                first = false;
                let _ = writeln!(out, "    {if_keyword} let Some(v) = config.pointer(\"/{section}/{var_snake}\").and_then(|v| v.as_f64()) {{");
                let _ = writeln!(out, "        model = model.{setter}(v);");
            }
            OneofInnerType::Bool => {
                first = false;
                let _ = writeln!(out, "    {if_keyword} let Some(v) = config.pointer(\"/{section}/{var_snake}\").and_then(|v| v.as_bool()) {{");
                let _ = writeln!(out, "        model = model.{setter}(v);");
            }
            OneofInnerType::SameCrateStruct(type_path) | OneofInnerType::CrossCrateStruct(type_path) => {
                first = false;
                // Both same-crate and cross-crate structs have custom serde in GCP SDKs.
                // Replace crate:: with the sdk crate module path.
                let full_path = type_path.replace("crate::", &format!("{sdk_crate_mod}::"));
                let _ = writeln!(out, "    {if_keyword} let Some(v) = config.pointer(\"/{section}/{var_snake}\") {{");
                let _ = writeln!(out, "        let parsed: {full_path} = serde_json::from_value(v.clone())");
                let _ = writeln!(out, "            .map_err(|e| ProviderError::InvalidConfig(format!(\"{var_snake}: {{e}}\")))?;");
                let _ = writeln!(out, "        model = model.{setter}(parsed);");
            }
        }
    }

    if !first {
        let _ = writeln!(out, "    }}");
    }
}

fn write_provider_id_parsing(out: &mut String, m: &ResourceManifest) {
    if m.resource.api_style == ApiStyle::ResourceName
        || m.resource.api_style == ApiStyle::DirectModel
    {
        // Resource-name style: provider_id IS the full resource name
        // Extract the short name from the last path segment
        let _ = writeln!(
            out,
            "    let name = provider_id.rsplit('/').next().unwrap_or(provider_id).to_string();"
        );
    } else {
        match m.resource.scope {
            Scope::Zonal => {
                let _ = writeln!(
                    out,
                    "    let (zone, name) = super::parse_zone_resource(provider_id, &self.region);"
                );
            }
            Scope::Regional => {
                let _ = writeln!(
                    out,
                    "    let (region, name) = super::parse_region_resource(provider_id, &self.region);"
                );
            }
            _ => {
                let _ = writeln!(
                    out,
                    "    let name = provider_id.to_string();"
                );
            }
        }
    }
}

fn write_provider_id_construction(out: &mut String, m: &ResourceManifest) {
    if m.resource.api_style == ApiStyle::ResourceName
        || m.resource.api_style == ApiStyle::DirectModel
    {
        let noun_plural = resource_noun_plural(m);

        if m.resource.parent_binding.is_some() {
            // Nested resource: provider_id = {parent}/{noun_plural}/{name}
            let _ = writeln!(
                out,
                "    let provider_id = format!(\"{{}}/{noun_plural}/{{}}\", parent, name);"
            );
        } else if let Some(ref parent_fmt) = m.resource.parent_format {
            let has_project = parent_fmt.contains("{project}");
            let has_location = parent_fmt.contains("{location}");

            if has_project && has_location {
                let _ = writeln!(
                    out,
                    "    let provider_id = format!(\"projects/{{}}/locations/{{}}/{noun_plural}/{{}}\", self.project_id, self.region, name);"
                );
            } else if has_project {
                let _ = writeln!(
                    out,
                    "    let provider_id = format!(\"projects/{{}}/{noun_plural}/{{}}\", self.project_id, name);"
                );
            } else {
                let _ = writeln!(out, "    let provider_id = name.to_string();");
            }
        } else {
            let _ = writeln!(out, "    let provider_id = name.to_string();");
        }
    } else {
        match m.resource.scope {
            Scope::Zonal => {
                let _ = writeln!(
                    out,
                    "    let provider_id = format!(\"{{zone}}/{{name}}\");"
                );
            }
            Scope::Regional => {
                let _ = writeln!(
                    out,
                    "    let provider_id = format!(\"{{region}}/{{name}}\");"
                );
            }
            _ => {
                let _ = writeln!(out, "    let provider_id = name.to_string();");
            }
        }
    }
}

/// Get the GCP resource path segment (e.g., "cryptoKeys", "nodePools").
/// Uses `resource_noun` if set, otherwise falls back to snake_case(model) + "s".
fn resource_noun_plural(m: &ResourceManifest) -> String {
    if let Some(ref noun) = m.resource.resource_noun {
        noun.clone()
    } else {
        let noun = snake_case(&m.resource.sdk_model);
        format!("{noun}s")
    }
}

/// Build the parent expression for resource_name style create calls.
fn build_parent_expr(m: &ResourceManifest) -> String {
    if let Some(ref binding) = m.resource.parent_binding {
        // Nested resource: parent is the parent resource's provider_id (from binding)
        let section = m
            .resource
            .parent_binding_section
            .as_deref()
            .unwrap_or("identity");
        format!(
            "config.require_str(\"/{section}/{binding}\")?.to_string()"
        )
    } else if let Some(ref parent_fmt) = m.resource.parent_format {
        let has_location = parent_fmt.contains("{location}");
        if has_location {
            "format!(\"projects/{}/locations/{}\", self.project_id, self.region)".into()
        } else {
            "format!(\"projects/{}\", self.project_id)".into()
        }
    } else {
        "format!(\"projects/{}\", self.project_id)".into()
    }
}

fn write_state_json(out: &mut String, m: &ResourceManifest, model_var: &str) {
    let sections = group_fields_by_section(&m.fields);

    // Filter labels from state
    if m.fields.contains_key("labels") {
        let _ = writeln!(out, "    let user_labels: serde_json::Map<String, serde_json::Value> = {model_var}.labels");
        let _ = writeln!(out, "        .iter()");
        if !m.resource.raw_labels {
            let _ = writeln!(
                out,
                "        .filter(|(k, _)| k.as_str() != \"managed_by\")"
            );
        }
        let _ = writeln!(
            out,
            "        .map(|(k, v)| (k.clone(), serde_json::json!(v)))"
        );
        let _ = writeln!(out, "        .collect();");
        let _ = writeln!(out);
    }

    let _ = writeln!(out, "    let state = serde_json::json!({{");

    for (section_name, fields) in &sections {
        let _ = writeln!(out, "        \"{section_name}\": {{");
        for (field_name, field) in fields {
            if field.output_only {
                continue;
            }
            let accessor = field_read_accessor(model_var, field_name, field);
            let _ = writeln!(out, "            \"{field_name}\": {accessor},");
        }
        let _ = writeln!(out, "        }},");
    }

    let _ = writeln!(out, "    }});");
    let _ = writeln!(out);
}

fn field_read_accessor(model_var: &str, field_name: &str, field: &FieldDef) -> String {
    // Custom read expression overrides all default accessor logic.
    if let Some(expr) = &field.read_expression {
        return expr.clone();
    }

    let sdk_field_raw = field
        .sdk_read_field
        .as_deref()
        .or(field.sdk_field.as_deref())
        .unwrap_or(field_name);
    // Use r#type syntax for Rust keywords used as struct field accessors
    let sdk_field = raw_field(sdk_field_raw);

    if field_name == "labels" {
        return "user_labels".into();
    }

    if field.optional {
        // Field is Option<T> in the SDK
        match field.field_type.as_str() {
            "String" => format!("{model_var}.{sdk_field}.as_deref().unwrap_or(\"\")"),
            "Bool" => format!("{model_var}.{sdk_field}.unwrap_or(false)"),
            "Integer" | "Integer_u32" | "Integer_u64" => format!("{model_var}.{sdk_field}.unwrap_or(0)"),
            "Float" => format!("{model_var}.{sdk_field}.unwrap_or(0.0)"),
            _ => {
                // Enum, Nested, Vec, HashMap, Duration, Timestamp — serialize via serde.
                // Only attempt serialization if we have a valid type path (meaning the type
                // was successfully resolved). Proto oneofs without Serialize get Null.
                if field.sdk_type_path.is_some() {
                    format!("&{model_var}.{sdk_field}")
                } else {
                    "serde_json::Value::Null".into()
                }
            }
        }
    } else {
        // Field is bare T in the SDK (not wrapped in Option)
        match field.field_type.as_str() {
            "String" => format!("{model_var}.{sdk_field}.as_str()"),
            "Bool" | "Integer" | "Integer_u32" | "Integer_u64" | "Float" => format!("{model_var}.{sdk_field}"),
            _ => {
                if field.sdk_type_path.is_some() {
                    format!("&{model_var}.{sdk_field}")
                } else {
                    "serde_json::Value::Null".into()
                }
            }
        }
    }
}

