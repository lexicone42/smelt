//! Generate Rust provider code from a resource manifest.
//!
//! Emits schema functions, CRUD implementations, and diff rules that follow
//! the patterns established in the hand-written smelt provider files.

use std::collections::BTreeMap;
use std::fmt::Write;

use crate::manifest::{FieldDef, ResourceManifest};

pub fn generate_provider_code(manifest: &ResourceManifest) -> String {
    let mut out = String::with_capacity(8192);

    write_header(&mut out, manifest);
    write_schema_fn(&mut out, manifest);
    write_create_fn(&mut out, manifest);
    write_read_fn(&mut out, manifest);
    write_update_fn(&mut out, manifest);
    write_delete_fn(&mut out, manifest);
    write_diff_rules(&mut out, manifest);

    out
}

// ── Header ──────────────────────────────────────────────────────────────────

fn write_header(out: &mut String, m: &ResourceManifest) {
    let _ = writeln!(out, "// Generated by smelt-codegen — do not edit by hand.");
    let _ = writeln!(
        out,
        "// Source manifest: {}.toml",
        m.resource.type_path.replace('.', "_").to_lowercase()
    );
    let _ = writeln!(out, "//");
    let _ = writeln!(out, "// Resource: {}", m.resource.type_path);
    let _ = writeln!(out, "// SDK: {} ({})", m.resource.sdk_crate, m.resource.sdk_model);
    let _ = writeln!(out);

    if m.resource.provider == "gcp" {
        let _ = writeln!(out, "use crate::provider::{{ConfigExt, ProviderError, ResourceOutput}};");
        let _ = writeln!(out, "use std::collections::HashMap;");
    } else {
        let _ = writeln!(out, "use crate::provider::{{ConfigExt, ProviderError, ResourceOutput}};");
        let _ = writeln!(out, "use std::collections::HashMap;");
    }
    let _ = writeln!(out);
}

// ── Schema function ─────────────────────────────────────────────────────────

fn write_schema_fn(out: &mut String, m: &ResourceManifest) {
    let fn_name = schema_fn_name(&m.resource.type_path);
    let sections = group_fields_by_section(&m.fields);

    let _ = writeln!(
        out,
        "pub(super) fn {fn_name}() -> crate::provider::ResourceTypeInfo {{"
    );
    let _ = writeln!(out, "    crate::provider::ResourceTypeInfo {{");
    let _ = writeln!(
        out,
        "        type_path: \"{}\".into(),",
        m.resource.type_path
    );
    let _ = writeln!(
        out,
        "        description: \"{}\".into(),",
        m.resource.description.replace('"', "\\\"")
    );
    let _ = writeln!(out, "        schema: crate::provider::ResourceSchema {{");
    let _ = writeln!(out, "            sections: vec![");

    for (section_name, fields) in &sections {
        let _ = writeln!(out, "                crate::provider::SectionSchema {{");
        let _ = writeln!(out, "                    name: \"{section_name}\".into(),");
        let _ = writeln!(
            out,
            "                    description: \"{} configuration\".into(),",
            capitalize(section_name)
        );
        let _ = writeln!(out, "                    fields: vec![");

        for (field_name, field_def) in fields {
            write_field_schema(out, field_name, field_def);
        }

        let _ = writeln!(out, "                    ],");
        let _ = writeln!(out, "                }},");
    }

    let _ = writeln!(out, "            ],");
    let _ = writeln!(out, "        }},");
    let _ = writeln!(out, "    }}");
    let _ = writeln!(out, "}}");
    let _ = writeln!(out);
}

fn write_field_schema(out: &mut String, name: &str, f: &FieldDef) {
    let _ = writeln!(out, "                        crate::provider::FieldSchema {{");
    let _ = writeln!(out, "                            name: \"{name}\".into(),");
    let _ = writeln!(
        out,
        "                            description: \"{}\".into(),",
        f.description
            .as_deref()
            .unwrap_or("")
            .replace('\\', "\\\\")
            .replace('"', "\\\"")
    );
    let _ = writeln!(
        out,
        "                            field_type: {},",
        field_type_expr(&f.field_type, &f.variants)
    );
    let _ = writeln!(
        out,
        "                            required: {},",
        f.required
    );

    if let Some(ref default) = f.default {
        let _ = writeln!(
            out,
            "                            default: Some(serde_json::json!({})),",
            toml_to_json_literal(default)
        );
    } else {
        let _ = writeln!(out, "                            default: None,");
    }

    let _ = writeln!(
        out,
        "                            sensitive: {},",
        f.sensitive
    );
    let _ = writeln!(out, "                        }},");
}

// ── Create function ─────────────────────────────────────────────────────────

fn write_create_fn(out: &mut String, m: &ResourceManifest) {
    let fn_name = crud_fn_name("create", &m.resource.type_path);
    let is_gcp = m.resource.provider == "gcp";

    let _ = writeln!(out, "pub(super) async fn {fn_name}(");
    let _ = writeln!(out, "    &self,");
    let _ = writeln!(out, "    config: &serde_json::Value,");
    let _ = writeln!(out, ") -> Result<ResourceOutput, ProviderError> {{");

    // Extract fields from config
    let sdk_crate_mod = sdk_crate_to_mod(&m.resource.sdk_crate);
    let _ = writeln!(out, "    // Extract fields from config");
    for (name, field) in &m.fields {
        if field.output_only || field.skip {
            continue;
        }
        write_field_extraction(out, name, field, &sdk_crate_mod);
    }
    let _ = writeln!(out);

    if is_gcp {
        write_gcp_create_body(out, m);
    } else {
        write_aws_create_body(out, m);
    }

    let _ = writeln!(out, "}}");
    let _ = writeln!(out);
}

fn write_gcp_create_body(out: &mut String, m: &ResourceManifest) {
    let model = &m.resource.sdk_model;
    let sdk_crate_mod = sdk_crate_to_mod(&m.resource.sdk_crate);
    let client_accessor = m.resource.client_accessor.clone().unwrap_or_else(|| snake_case(&m.resource.sdk_client));
    let create_method = &m.crud.create;
    let is_resource_name = m.resource.api_style == "resource_name";

    // Extract labels if any identity.labels field
    if m.fields.contains_key("labels") {
        let _ = writeln!(out, "    let labels = super::extract_labels(config);");
    }

    let _ = writeln!(out, "    // Build SDK model");
    let _ = writeln!(
        out,
        "    let mut model = {sdk_crate_mod}::model::{model}::default();"
    );

    // Set fields on the model
    for (name, field) in &m.fields {
        if field.output_only || field.skip || name == "labels" {
            continue;
        }
        let sdk_field = field.sdk_field.as_deref().unwrap_or(name);
        let setter = format!("set_{sdk_field}");
        write_model_setter(out, name, &setter, field, &sdk_crate_mod);
    }

    // Labels
    if m.fields.contains_key("labels") {
        let _ = writeln!(out, "    model = model.set_labels(labels);");
    }

    let _ = writeln!(out);
    let _ = writeln!(out, "    // Make API call");

    if is_resource_name {
        // Resource-name style: set_parent + set_resource_id + set_resource(model)
        let parent = build_parent_expr(m);
        let parent_setter = m.resource.parent_setter.as_deref().unwrap_or("set_parent");
        let _ = writeln!(out, "    let parent = {parent};");
        let _ = writeln!(out, "    self.{client_accessor}().await?");
        let _ = writeln!(out, "        .{create_method}()");
        let _ = writeln!(out, "        .{parent_setter}(&parent)");

        if let Some(ref id_setter) = m.resource.resource_id_setter {
            let _ = writeln!(out, "        .{id_setter}(&name)");
        }

        let body_setter = m.resource.resource_body_setter.as_deref().unwrap_or("set_body");
        let _ = writeln!(out, "        .{body_setter}(model)");
    } else {
        // Compute style: set_project + set_zone/region + set_body
        match m.resource.scope.as_str() {
            "zonal" => {
                let _ = writeln!(out, "    let zone = config.str_or(\"/sizing/zone\", &format!(\"{{}}-a\", self.region)).to_string();");
            }
            "regional" => {
                let _ = writeln!(out, "    let region = config.str_or(\"/network/region\", &self.region).to_string();");
            }
            _ => {}
        }
        let _ = writeln!(out, "    self.{client_accessor}().await?");
        let _ = writeln!(out, "        .{create_method}()");
        let _ = writeln!(out, "        .set_project(&self.project_id)");

        match m.resource.scope.as_str() {
            "zonal" => {
                let _ = writeln!(out, "        .set_zone(&zone)");
            }
            "regional" => {
                let _ = writeln!(out, "        .set_region(&region)");
            }
            _ => {}
        }

        let _ = writeln!(out, "        .set_body(model)");
    }

    let _ = writeln!(out, "        .send()");
    let _ = writeln!(out, "        .await");
    let _ = writeln!(
        out,
        "        .map_err(|e| super::classify_gcp_error(\"{} {}\", e))?;",
        capitalize(&m.crud.create),
        m.resource.sdk_model
    );
    let _ = writeln!(out);

    // Build provider_id and read back
    write_provider_id_construction(out, m);
    let read_fn = crud_fn_name("read", &m.resource.type_path);
    let _ = writeln!(out, "    self.{read_fn}(&provider_id).await");
}

fn write_aws_create_body(out: &mut String, m: &ResourceManifest) {
    let create_method = snake_case(&m.crud.create);

    let _ = writeln!(out, "    // Make API call");
    let _ = writeln!(
        out,
        "    let result = self.client"
    );
    let _ = writeln!(out, "        .{create_method}()");

    for (name, field) in &m.fields {
        if field.output_only || field.skip {
            continue;
        }
        let sdk_field = field.sdk_field.as_deref().unwrap_or(name);
        let setter = snake_case(sdk_field);
        let _ = writeln!(
            out,
            "        .{setter}({name})"
        );
    }

    let _ = writeln!(out, "        .send()");
    let _ = writeln!(out, "        .await");
    let _ = writeln!(
        out,
        "        .map_err(|e| ProviderError::ApiError(format!(\"{} {}: {{e}}\")))?;",
        capitalize(&m.crud.create),
        m.resource.sdk_model
    );
    let _ = writeln!(out);

    write_provider_id_construction(out, m);
    let read_fn = crud_fn_name("read", &m.resource.type_path);
    let _ = writeln!(out, "    self.{read_fn}(&provider_id).await");
}

// ── Read function ───────────────────────────────────────────────────────────

fn write_read_fn(out: &mut String, m: &ResourceManifest) {
    let fn_name = crud_fn_name("read", &m.resource.type_path);
    let is_gcp = m.resource.provider == "gcp";

    let _ = writeln!(out, "pub(super) async fn {fn_name}(");
    let _ = writeln!(out, "    &self,");
    let _ = writeln!(out, "    provider_id: &str,");
    let _ = writeln!(out, ") -> Result<ResourceOutput, ProviderError> {{");

    // Parse provider_id
    write_provider_id_parsing(out, m);

    if is_gcp {
        write_gcp_read_body(out, m);
    } else {
        write_aws_read_body(out, m);
    }

    let _ = writeln!(out, "}}");
    let _ = writeln!(out);
}

fn write_gcp_read_body(out: &mut String, m: &ResourceManifest) {
    let model_var = snake_case(&m.resource.sdk_model);
    let client_accessor = m.resource.client_accessor.clone().unwrap_or_else(|| snake_case(&m.resource.sdk_client));
    let read_method = &m.crud.read;
    let model_name = &m.resource.sdk_model;
    let is_resource_name = m.resource.api_style == "resource_name";

    let _ = writeln!(out, "    let {model_var} = self");
    let _ = writeln!(out, "        .{client_accessor}().await?");
    let _ = writeln!(out, "        .{read_method}()");

    if is_resource_name {
        // Resource-name style: set_name (or override) with the full resource path
        let name_setter = m.resource.resource_name_param.as_deref().unwrap_or("set_name");
        let _ = writeln!(out, "        .{name_setter}(provider_id)");
    } else {
        // Compute style: set_project + scope + resource name
        let _ = writeln!(out, "        .set_project(&self.project_id)");
        match m.resource.scope.as_str() {
            "zonal" => {
                let _ = writeln!(out, "        .set_zone(&zone)");
            }
            "regional" => {
                let _ = writeln!(out, "        .set_region(&region)");
            }
            _ => {}
        }
        let resource_setter = m.resource.resource_id_param.clone()
            .unwrap_or_else(|| format!("set_{}", snake_case(&m.resource.sdk_model)));
        let _ = writeln!(out, "        .{resource_setter}(&name)");
    }

    let _ = writeln!(out, "        .send()");
    let _ = writeln!(out, "        .await");
    let _ = writeln!(
        out,
        "        .map_err(|e| super::classify_gcp_error(\"Get{model_name}\", e))?;"
    );
    let _ = writeln!(out);

    // Build state JSON
    write_state_json(out, m, &model_var);

    // Build outputs
    let _ = writeln!(out, "    let mut outputs = HashMap::new();");
    if let Some(ref output_field) = m.resource.output_field {
        // Custom output field specified
        let _ = writeln!(
            out,
            "    outputs.insert(\"{output_field}\".into(), serde_json::json!(&{model_var}.{output_field}));"
        );
    } else if is_resource_name {
        let _ = writeln!(
            out,
            "    outputs.insert(\"name\".into(), serde_json::json!(&{model_var}.name));"
        );
    } else {
        let _ = writeln!(
            out,
            "    outputs.insert(\"self_link\".into(), serde_json::json!({model_var}.self_link.as_deref().unwrap_or(\"\")));"
        );
    }
    let _ = writeln!(out);

    let _ = writeln!(out, "    Ok(ResourceOutput {{");
    let _ = writeln!(out, "        provider_id: provider_id.to_string(),");
    let _ = writeln!(out, "        state,");
    let _ = writeln!(out, "        outputs,");
    let _ = writeln!(out, "    }})");
}

fn write_aws_read_body(out: &mut String, m: &ResourceManifest) {
    let model_var = snake_case(&m.resource.sdk_model);
    let read_method = snake_case(&m.crud.read);

    let _ = writeln!(out, "    let response = self.client");
    let _ = writeln!(out, "        .{read_method}()");
    let _ = writeln!(out, "        // TODO: set identifier parameters");
    let _ = writeln!(out, "        .send()");
    let _ = writeln!(out, "        .await");
    let _ = writeln!(
        out,
        "        .map_err(|e| ProviderError::ApiError(format!(\"Read {{}}: {{e}}\", provider_id)))?;"
    );
    let _ = writeln!(out);
    let _ = writeln!(out, "    let {model_var} = response; // TODO: extract from response");
    let _ = writeln!(out);

    write_state_json(out, m, &model_var);

    let _ = writeln!(out, "    let mut outputs = HashMap::new();");
    let _ = writeln!(out);

    let _ = writeln!(out, "    Ok(ResourceOutput {{");
    let _ = writeln!(out, "        provider_id: provider_id.to_string(),");
    let _ = writeln!(out, "        state,");
    let _ = writeln!(out, "        outputs,");
    let _ = writeln!(out, "    }})");
}

// ── Update function ─────────────────────────────────────────────────────────

fn write_update_fn(out: &mut String, m: &ResourceManifest) {
    let fn_name = crud_fn_name("update", &m.resource.type_path);

    let _ = writeln!(out, "pub(super) async fn {fn_name}(");
    let _ = writeln!(out, "    &self,");
    let _ = writeln!(out, "    provider_id: &str,");
    let _ = writeln!(out, "    config: &serde_json::Value,");
    let _ = writeln!(out, ") -> Result<ResourceOutput, ProviderError> {{");

    if m.crud.update.is_some() {
        write_provider_id_parsing(out, m);

        if m.resource.provider == "gcp" {
            write_gcp_update_body(out, m);
        } else {
            write_aws_update_body(out, m);
        }
    } else {
        let _ = writeln!(
            out,
            "    Err(ProviderError::RequiresReplacement(\"resource does not support in-place update\".into()))"
        );
    }

    let _ = writeln!(out, "}}");
    let _ = writeln!(out);
}

fn write_gcp_update_body(out: &mut String, m: &ResourceManifest) {
    let update_method = m.crud.update.as_deref().unwrap_or("patch");
    let sdk_crate_mod = sdk_crate_to_mod(&m.resource.sdk_crate);
    let client_accessor = m.resource.client_accessor.clone().unwrap_or_else(|| snake_case(&m.resource.sdk_client));
    let model_name = &m.resource.sdk_model;
    let model = &m.resource.sdk_model;
    let is_resource_name = m.resource.api_style == "resource_name";

    // Re-extract fields from config for update
    let _ = writeln!(out, "    // Extract fields from config");
    for (name, field) in &m.fields {
        if field.output_only || field.skip || name == "labels" || name == "name" || name == "zone" || name == "region" {
            continue;
        }
        write_field_extraction(out, name, field, &sdk_crate_mod);
    }

    if m.fields.contains_key("labels") {
        let _ = writeln!(out, "    let labels = super::extract_labels(config);");
    }
    let _ = writeln!(out);

    let _ = writeln!(
        out,
        "    let mut model = {sdk_crate_mod}::model::{model}::default();"
    );

    // For resource_name style, set the name on the model
    if is_resource_name {
        let _ = writeln!(out, "    model = model.set_name(provider_id);");
    }

    // Set updatable fields
    for (name, field) in &m.fields {
        if field.output_only || field.skip || name == "labels" || name == "name" || name == "zone" || name == "region" {
            continue;
        }
        let sdk_field = field.sdk_field.as_deref().unwrap_or(name);
        let setter = format!("set_{sdk_field}");
        write_model_setter(out, name, &setter, field, &sdk_crate_mod);
    }

    if m.fields.contains_key("labels") {
        let _ = writeln!(out, "    model = model.set_labels(labels);");
    }
    let _ = writeln!(out);

    let _ = writeln!(out, "    self.{client_accessor}().await?");
    let _ = writeln!(out, "        .{update_method}()");

    if is_resource_name {
        // Resource-name style: set the model body (which contains the name)
        let body_setter = m.resource.resource_body_setter.as_deref().unwrap_or("set_body");
        let _ = writeln!(out, "        .{body_setter}(model)");
        if m.resource.has_update_mask {
            let _ = writeln!(out, "        .set_update_mask(google_cloud_wkt::FieldMask::default())");
        }
    } else {
        // Compute style: set_project + scope + resource name + body
        let _ = writeln!(out, "        .set_project(&self.project_id)");
        match m.resource.scope.as_str() {
            "zonal" => {
                let _ = writeln!(out, "        .set_zone(&zone)");
            }
            "regional" => {
                let _ = writeln!(out, "        .set_region(&region)");
            }
            _ => {}
        }
        let resource_setter = m.resource.resource_id_param.clone()
            .unwrap_or_else(|| format!("set_{}", snake_case(&m.resource.sdk_model)));
        let _ = writeln!(out, "        .{resource_setter}(&name)");
        let _ = writeln!(out, "        .set_body(model)");
    }

    let _ = writeln!(out, "        .send()");
    let _ = writeln!(out, "        .await");
    let _ = writeln!(
        out,
        "        .map_err(|e| super::classify_gcp_error(\"{update_method} {model_name}\", e))?;"
    );
    let _ = writeln!(out);

    let read_fn = crud_fn_name("read", &m.resource.type_path);
    let _ = writeln!(out, "    self.{read_fn}(provider_id).await");
}

fn write_aws_update_body(out: &mut String, m: &ResourceManifest) {
    let update_method = snake_case(m.crud.update.as_deref().unwrap_or("modify"));

    let _ = writeln!(out, "    self.client");
    let _ = writeln!(out, "        .{update_method}()");
    let _ = writeln!(out, "        // TODO: set fields from config");
    let _ = writeln!(out, "        .send()");
    let _ = writeln!(out, "        .await");
    let _ = writeln!(
        out,
        "        .map_err(|e| ProviderError::ApiError(format!(\"Update: {{e}}\")))?;"
    );
    let _ = writeln!(out);

    let read_fn = crud_fn_name("read", &m.resource.type_path);
    let _ = writeln!(out, "    self.{read_fn}(provider_id).await");
}

// ── Delete function ─────────────────────────────────────────────────────────

fn write_delete_fn(out: &mut String, m: &ResourceManifest) {
    let fn_name = crud_fn_name("delete", &m.resource.type_path);

    let _ = writeln!(out, "pub(super) async fn {fn_name}(");
    let _ = writeln!(out, "    &self,");
    let _ = writeln!(out, "    provider_id: &str,");
    let _ = writeln!(out, ") -> Result<(), ProviderError> {{");

    if m.crud.delete.is_none() {
        let _ = writeln!(out, "    Err(ProviderError::ApiError(\"delete not supported for this resource type\".into()))");
        let _ = writeln!(out, "}}");
        let _ = writeln!(out);
        return;
    }

    write_provider_id_parsing(out, m);

    if m.resource.provider == "gcp" {
        let client_accessor = m.resource.client_accessor.clone().unwrap_or_else(|| snake_case(&m.resource.sdk_client));
        let model_name = &m.resource.sdk_model;
        let delete_method = m.crud.delete.as_deref().unwrap();
        let is_resource_name = m.resource.api_style == "resource_name";

        let _ = writeln!(out, "    self.{client_accessor}().await?");
        let _ = writeln!(out, "        .{delete_method}()");

        if is_resource_name {
            let name_setter = m.resource.resource_name_param.as_deref().unwrap_or("set_name");
            let _ = writeln!(out, "        .{name_setter}(provider_id)");
        } else {
            let _ = writeln!(out, "        .set_project(&self.project_id)");
            match m.resource.scope.as_str() {
                "zonal" => {
                    let _ = writeln!(out, "        .set_zone(&zone)");
                }
                "regional" => {
                    let _ = writeln!(out, "        .set_region(&region)");
                }
                _ => {}
            }
            let resource_setter = m.resource.resource_id_param.clone()
                .unwrap_or_else(|| format!("set_{}", snake_case(&m.resource.sdk_model)));
            let _ = writeln!(out, "        .{resource_setter}(&name)");
        }

        let _ = writeln!(out, "        .send()");
        let _ = writeln!(out, "        .await");
        let _ = writeln!(
            out,
            "        .map_err(|e| super::classify_gcp_error(\"Delete{model_name}\", e))?;"
        );
    } else {
        let _ = writeln!(out, "    self.client");
        let _ = writeln!(out, "        .delete()");
        let _ = writeln!(out, "        // TODO: set identifier");
        let _ = writeln!(out, "        .send()");
        let _ = writeln!(out, "        .await");
        let _ = writeln!(
            out,
            "        .map_err(|e| ProviderError::ApiError(format!(\"Delete: {{e}}\")))?;"
        );
    }

    let _ = writeln!(out, "    Ok(())");
    let _ = writeln!(out, "}}");
    let _ = writeln!(out);
}

// ── Diff rules ──────────────────────────────────────────────────────────────

fn write_diff_rules(out: &mut String, m: &ResourceManifest) {
    if m.replacement_fields.is_empty() {
        return;
    }

    let _ = writeln!(out, "// Diff: fields that force replacement");
    let _ = writeln!(
        out,
        "pub(super) fn {}_forces_replacement(path: &str) -> bool {{",
        m.resource.type_path.replace('.', "_").to_lowercase()
    );
    let _ = writeln!(out, "    matches!(path,");

    for (i, field) in m.replacement_fields.iter().enumerate() {
        // Find section for this field
        let section = m
            .fields
            .get(field)
            .map(|f| f.section.as_str())
            .unwrap_or("identity");
        let trailing = if i < m.replacement_fields.len() - 1 {
            " |"
        } else {
            ""
        };
        let _ = writeln!(out, "        \"{section}.{field}\"{trailing}");
    }

    let _ = writeln!(out, "    )");
    let _ = writeln!(out, "}}");
}

