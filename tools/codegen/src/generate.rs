/// Convert SDK crate name to Rust module path.
/// "google-cloud-compute-v1" → "google_cloud_compute_v1"
/// "google-cloud-secretmanager-v1" → "google_cloud_secretmanager_v1"
fn sdk_crate_to_mod(sdk_crate: &str) -> String {
    sdk_crate.replace('-', "_")
}

fn schema_fn_name(type_path: &str) -> String {
    format!(
        "{}_schema",
        type_path.replace('.', "_").to_lowercase()
    )
}

fn crud_fn_name(operation: &str, type_path: &str) -> String {
    format!(
        "{operation}_{}",
        type_path.replace('.', "_").to_lowercase()
    )
}

fn capitalize(s: &str) -> String {
    let mut chars = s.chars();
    match chars.next() {
        None => String::new(),
        Some(c) => c.to_uppercase().chain(chars).collect(),
    }
}

fn field_type_expr(type_str: &str, variants: &[String]) -> String {
    match type_str {
        "String" => "crate::provider::FieldType::String".into(),
        "Bool" => "crate::provider::FieldType::Bool".into(),
        "Integer" | "Integer_u32" | "Integer_u64" => "crate::provider::FieldType::Integer".into(),
        "Float" => "crate::provider::FieldType::Float".into(),
        s if s.starts_with("Enum(") => {
            if variants.is_empty() {
                "crate::provider::FieldType::String /* TODO: Enum variants */".into()
            } else {
                let vs: Vec<String> = variants
                    .iter()
                    .map(|v| format!("\"{v}\".into()"))
                    .collect();
                format!(
                    "crate::provider::FieldType::Enum(vec![{}])",
                    vs.join(", ")
                )
            }
        }
        s if s.starts_with("Array(") => {
            let inner = &s[6..s.len() - 1];
            format!(
                "crate::provider::FieldType::Array(Box::new({}))",
                field_type_expr(inner, variants)
            )
        }
        "Ref" => "crate::provider::FieldType::Ref(\"*\".into())".into(),
        "Record" => "crate::provider::FieldType::Record(vec![])".into(),
        s if s.starts_with("Oneof(") || s.starts_with("Nested(") => "crate::provider::FieldType::Record(vec![])".into(),
        "Duration" | "Timestamp" | "Bytes" => "crate::provider::FieldType::String".into(),
        _ => format!("crate::provider::FieldType::String /* {type_str} */"),
    }
}

fn toml_to_json_literal(v: &toml::Value) -> String {
    match v {
        toml::Value::String(s) => {
            // Use Rust debug formatting which properly escapes " and \
            format!("{s:?}")
        }
        toml::Value::Integer(i) => format!("{i}"),
        toml::Value::Float(f) => format!("{f}"),
        toml::Value::Boolean(b) => format!("{b}"),
        _ => "null".into(),
    }
}

/// Group fields by section, preserving field order within each section.
fn group_fields_by_section(
    fields: &BTreeMap<String, FieldDef>,
) -> Vec<(String, Vec<(&String, &FieldDef)>)> {
    let mut sections: BTreeMap<String, Vec<(&String, &FieldDef)>> = BTreeMap::new();
    for (name, field) in fields {
        if field.skip {
            continue;
        }
        sections
            .entry(field.section.clone())
            .or_default()
            .push((name, field));
    }
    // Put "identity" first, then alphabetical
    let mut result = Vec::new();
    if let Some(identity) = sections.remove("identity") {
        result.push(("identity".into(), identity));
    }
    for (section, fields) in sections {
        result.push((section, fields));
    }
    result
}

fn write_field_extraction(out: &mut String, name: &str, field: &FieldDef, sdk_crate_mod: &str) {
    let section = &field.section;
    let path = format!("/{section}/{name}");
    let var = safe_ident(name);

    match field.field_type.as_str() {
        "String" => {
            if field.required {
                let _ = writeln!(
                    out,
                    "    let {var} = config.require_str(\"{path}\")?.to_string();"
                );
            } else if let Some(ref default) = field.default {
                let _ = writeln!(
                    out,
                    "    let {var} = config.str_or(\"{path}\", {}).to_string();",
                    toml_to_json_literal(default)
                );
            } else {
                let _ = writeln!(
                    out,
                    "    let {var} = config.optional_str(\"{path}\").map(String::from);"
                );
            }
        }
        "Bool" => {
            if field.required {
                let _ = writeln!(
                    out,
                    "    let {var} = config.require_bool(\"{path}\")?;"
                );
            } else if let Some(ref default) = field.default {
                let _ = writeln!(
                    out,
                    "    let {var} = config.bool_or(\"{path}\", {});",
                    toml_to_json_literal(default)
                );
            } else {
                let _ = writeln!(
                    out,
                    "    let {var} = config.optional_bool(\"{path}\");"
                );
            }
        }
        "Integer" | "Integer_u32" | "Integer_u64" => {
            if field.required {
                let _ = writeln!(
                    out,
                    "    let {var} = config.require_i64(\"{path}\")?;"
                );
            } else if let Some(ref default) = field.default {
                let _ = writeln!(
                    out,
                    "    let {var} = config.i64_or(\"{path}\", {});",
                    toml_to_json_literal(default)
                );
            } else {
                let _ = writeln!(
                    out,
                    "    let {var} = config.optional_i64(\"{path}\");"
                );
            }
        }
        "Float" => {
            // Float extraction — use pointer + as_f64
            let _ = writeln!(
                out,
                "    let {var} = config.pointer(\"{path}\").and_then(|v| v.as_f64());"
            );
        }
        s if s.starts_with("Enum(") => {
            // Enums are extracted as strings from config, then converted via from_name() in the setter
            if field.required {
                let _ = writeln!(
                    out,
                    "    let {var} = config.require_str(\"{path}\")?.to_string();"
                );
            } else {
                let _ = writeln!(
                    out,
                    "    let {var} = config.optional_str(\"{path}\").map(String::from);"
                );
            }
        }
        s if s.starts_with("Oneof(") => {
            // Oneof fields: extraction is done inline in write_model_setter
            // Nothing to extract here
        }
        _ => {
            // Nested, Array, Record, Duration, Timestamp — deserialize from config JSON
            if let Some(ref type_path) = field.sdk_type_path {
                let full_type = type_path.replace("crate::", &format!("{sdk_crate_mod}::"));
                let _ = writeln!(
                    out,
                    "    let {var} = config.pointer(\"{path}\").and_then(|v| serde_json::from_value::<{full_type}>(v.clone()).ok());"
                );
            } else {
                let _ = writeln!(
                    out,
                    "    // TODO: extract {var} ({}) from config[\"{path}\"] — type path unknown",
                    field.field_type
                );
            }
        }
    }
}

fn write_model_setter(out: &mut String, name: &str, setter: &str, field: &FieldDef, sdk_crate_mod: &str) {
    let var = safe_ident(name);
    match field.field_type.as_str() {
        "String" => {
            if field.required {
                let _ = writeln!(out, "    model = model.{setter}({var}.clone());");
            } else {
                let _ = writeln!(out, "    if let Some(v) = {var} {{");
                let _ = writeln!(out, "        model = model.{setter}(v);");
                let _ = writeln!(out, "    }}");
            }
        }
        "Bool" => {
            if field.required {
                let _ = writeln!(out, "    model = model.{setter}({var});");
            } else {
                let _ = writeln!(out, "    if let Some(v) = {var} {{");
                let _ = writeln!(out, "        model = model.{setter}(v);");
                let _ = writeln!(out, "    }}");
            }
        }
        "Integer" | "Integer_u32" | "Integer_u64" => {
            // Checked cast from extracted i64 to the SDK's expected integer type
            let cast_type = match field.field_type.as_str() {
                "Integer_u32" => "u32",
                "Integer_u64" => "u64",
                _ => "i32", // default: most SDK fields use i32
            };
            if field.required {
                let _ = writeln!(out, "    model = model.{setter}({cast_type}::try_from({var}).map_err(|_| ProviderError::InvalidConfig(format!(\"{name}: value {{}} out of range for {cast_type}\", {var})))?);");
            } else {
                let _ = writeln!(out, "    if let Some(v) = {var} {{");
                let _ = writeln!(out, "        model = model.{setter}({cast_type}::try_from(v).map_err(|_| ProviderError::InvalidConfig(format!(\"{name}: value {{v}} out of range for {cast_type}\")))?);");
                let _ = writeln!(out, "    }}");
            }
        }
        "Float" => {
            // f64→f32 is lossy but there's no TryFrom; `as f32` is acceptable
            // for the percentage/ratio values in GCP APIs
            if field.required {
                let _ = writeln!(out, "    model = model.{setter}({var} as f32);");
            } else {
                let _ = writeln!(out, "    if let Some(v) = {var} {{");
                let _ = writeln!(out, "        model = model.{setter}(v as f32);");
                let _ = writeln!(out, "    }}");
            }
        }
        s if s.starts_with("Enum(") => {
            // Convert string to SDK enum via From<&str>
            if let Some(ref type_path) = field.sdk_type_path {
                let full_path = type_path.replace("crate::", &format!("{sdk_crate_mod}::"));
                if field.required {
                    // required field: var was extracted as String
                    let _ = writeln!(out, "    model = model.{setter}({full_path}::from({var}.as_str()));");
                } else {
                    // optional field: var was extracted as Option<String>
                    let _ = writeln!(out, "    if let Some(ref s) = {var} {{");
                    let _ = writeln!(out, "        model = model.{setter}({full_path}::from(s.as_str()));");
                    let _ = writeln!(out, "    }}");
                }
            } else {
                let _ = writeln!(out, "    // TODO: set {name} on model via .{setter}() — enum type path unknown");
            }
        }
        s if s.starts_with("Oneof(") => {
            // Proto oneof: check each variant's config path and call the variant-specific setter
            if field.oneof_variants.is_empty() {
                let _ = writeln!(out, "    // TODO: set {name} on model via .{setter}() — no oneof variants parsed");
                return;
            }
            write_oneof_setters(out, name, &field.section, &field.oneof_variants, sdk_crate_mod);
        }
        _ => {
            // Nested, Array, Record, Duration, Timestamp — already extracted as Option<T>
            if field.sdk_type_path.is_some() {
                let _ = writeln!(out, "    if let Some(v) = {var} {{");
                let _ = writeln!(out, "        model = model.{setter}(v);");
                let _ = writeln!(out, "    }}");
            } else {
                let _ = writeln!(out, "    // TODO: set {name} on model via .{setter}() — type path unknown");
            }
        }
    }
}

/// Write a setter call that chains on a request builder (for direct_model style).
/// Used in create/update where setters are called on the builder, not on a model variable.
fn write_request_setter(out: &mut String, name: &str, setter: &str, field: &FieldDef) {
    let var = safe_ident(name);
    match field.field_type.as_str() {
        "String" => {
            if field.required {
                let _ = writeln!(out, "        .{setter}({var}.clone())");
            } else {
                // Optional strings: wrap in set_or_clear with Option
                let _ = writeln!(out, "        .set_or_clear_{setter}({var})", setter = setter.strip_prefix("set_").unwrap_or(setter));
            }
        }
        "Bool" => {
            if field.required {
                let _ = writeln!(out, "        .{setter}({var})");
            } else {
                let _ = writeln!(out, "        // TODO: set optional bool {name} via .{setter}()");
            }
        }
        "Integer" | "Integer_u32" | "Integer_u64" => {
            let cast_type = match field.field_type.as_str() {
                "Integer_u32" => "u32",
                "Integer_u64" => "u64",
                _ => "i32",
            };
            if field.required {
                let _ = writeln!(out, "        .{setter}({var} as {cast_type})");
            } else {
                let _ = writeln!(out, "        // TODO: set optional {cast_type} {name} via .{setter}()");
            }
        }
        _ => {
            let _ = writeln!(out, "        // TODO: set {name} via .{setter}()");
        }
    }
}

/// Generate combined extraction+setter code for each oneof variant.
///
/// For a field "expiration" with variants [ExpireTime(Timestamp), Ttl(Duration)]:
/// ```ignore
/// if let Some(v) = config.optional_str("/config/expire_time") {
///     model = model.set_expire_time(google_cloud_wkt::Timestamp::try_from(v)
///         .map_err(|e| ProviderError::InvalidConfig(format!("expire_time: {e}")))?);
/// } else if let Some(v) = config.optional_str("/config/ttl") {
///     model = model.set_ttl(google_cloud_wkt::Duration::try_from(v)
///         .map_err(|e| ProviderError::InvalidConfig(format!("ttl: {e}")))?);
/// }
/// ```
fn write_oneof_setters(
    out: &mut String,
    _field_name: &str,
    section: &str,
    variants: &[crate::introspect::OneofVariant],
    sdk_crate_mod: &str,
) {
    use crate::introspect::OneofInnerType;

    let mut first = true;
    for variant in variants {
        let setter = &variant.setter;
        let var_snake = &variant.setter[4..]; // strip "set_" prefix for config path

        let if_keyword = if first { "if" } else { "} else if" };

        match &variant.inner_type {
            OneofInnerType::String => {
                first = false;
                let _ = writeln!(out, "    {if_keyword} let Some(v) = config.optional_str(\"/{section}/{var_snake}\") {{");
                let _ = writeln!(out, "        model = model.{setter}(v);");
            }
            OneofInnerType::Timestamp => {
                first = false;
                let _ = writeln!(out, "    {if_keyword} let Some(v) = config.optional_str(\"/{section}/{var_snake}\") {{");
                let _ = writeln!(out, "        model = model.{setter}(google_cloud_wkt::Timestamp::try_from(v)");
                let _ = writeln!(out, "            .map_err(|e| ProviderError::InvalidConfig(format!(\"{var_snake}: {{e}}\")))?);");
            }
            OneofInnerType::Duration => {
                first = false;
                let _ = writeln!(out, "    {if_keyword} let Some(v) = config.optional_str(\"/{section}/{var_snake}\") {{");
                let _ = writeln!(out, "        model = model.{setter}(google_cloud_wkt::Duration::try_from(v)");
                let _ = writeln!(out, "            .map_err(|e| ProviderError::InvalidConfig(format!(\"{var_snake}: {{e}}\")))?);");
            }
            OneofInnerType::Integer(int_type) => {
                first = false;
                let _ = writeln!(out, "    {if_keyword} let Some(v) = config.pointer(\"/{section}/{var_snake}\").and_then(|v| v.as_i64()) {{");
                let _ = writeln!(out, "        model = model.{setter}({int_type}::try_from(v)");
                let _ = writeln!(out, "            .map_err(|_| ProviderError::InvalidConfig(format!(\"{var_snake}: value {{v}} out of range for {int_type}\")))?);");
            }
            OneofInnerType::Float => {
                first = false;
                let _ = writeln!(out, "    {if_keyword} let Some(v) = config.pointer(\"/{section}/{var_snake}\").and_then(|v| v.as_f64()) {{");
                let _ = writeln!(out, "        model = model.{setter}(v);");
            }
            OneofInnerType::Bool => {
                first = false;
                let _ = writeln!(out, "    {if_keyword} let Some(v) = config.pointer(\"/{section}/{var_snake}\").and_then(|v| v.as_bool()) {{");
                let _ = writeln!(out, "        model = model.{setter}(v);");
            }
            OneofInnerType::SameCrateStruct(type_path) | OneofInnerType::CrossCrateStruct(type_path) => {
                first = false;
                // Both same-crate and cross-crate structs have custom serde in GCP SDKs.
                // Replace crate:: with the sdk crate module path.
                let full_path = type_path.replace("crate::", &format!("{sdk_crate_mod}::"));
                let _ = writeln!(out, "    {if_keyword} let Some(v) = config.pointer(\"/{section}/{var_snake}\") {{");
                let _ = writeln!(out, "        let parsed: {full_path} = serde_json::from_value(v.clone())");
                let _ = writeln!(out, "            .map_err(|e| ProviderError::InvalidConfig(format!(\"{var_snake}: {{e}}\")))?;");
                let _ = writeln!(out, "        model = model.{setter}(parsed);");
            }
        }
    }

    if !first {
        let _ = writeln!(out, "    }}");
    }
}

fn write_provider_id_parsing(out: &mut String, m: &ResourceManifest) {
    if m.resource.api_style == ApiStyle::ResourceName
        || m.resource.api_style == ApiStyle::DirectModel
    {
        // Resource-name style: provider_id IS the full resource name
        // Extract the short name from the last path segment
        let _ = writeln!(
            out,
            "    let name = provider_id.rsplit('/').next().unwrap_or(provider_id).to_string();"
        );
    } else {
        match m.resource.scope {
            Scope::Zonal => {
                let _ = writeln!(
                    out,
                    "    let (zone, name) = super::parse_zone_resource(provider_id, &self.region);"
                );
            }
            Scope::Regional => {
                let _ = writeln!(
                    out,
                    "    let (region, name) = super::parse_region_resource(provider_id, &self.region);"
                );
            }
            _ => {
                let _ = writeln!(
                    out,
                    "    let name = provider_id.to_string();"
                );
            }
        }
    }
}

fn write_provider_id_construction(out: &mut String, m: &ResourceManifest) {
    if m.resource.api_style == ApiStyle::ResourceName
        || m.resource.api_style == ApiStyle::DirectModel
    {
        let noun_plural = resource_noun_plural(m);

        if m.resource.parent_binding.is_some() {
            // Nested resource: provider_id = {parent}/{noun_plural}/{name}
            let _ = writeln!(
                out,
                "    let provider_id = format!(\"{{}}/{noun_plural}/{{}}\", parent, name);"
            );
        } else if let Some(ref parent_fmt) = m.resource.parent_format {
            let has_project = parent_fmt.contains("{project}");
            let has_location = parent_fmt.contains("{location}");

            if has_project && has_location {
                let _ = writeln!(
                    out,
                    "    let provider_id = format!(\"projects/{{}}/locations/{{}}/{noun_plural}/{{}}\", self.project_id, self.region, name);"
                );
            } else if has_project {
                let _ = writeln!(
                    out,
                    "    let provider_id = format!(\"projects/{{}}/{noun_plural}/{{}}\", self.project_id, name);"
                );
            } else {
                let _ = writeln!(out, "    let provider_id = name.to_string();");
            }
        } else {
            let _ = writeln!(out, "    let provider_id = name.to_string();");
        }
    } else {
        match m.resource.scope {
            Scope::Zonal => {
                let _ = writeln!(
                    out,
                    "    let provider_id = format!(\"{{zone}}/{{name}}\");"
                );
            }
            Scope::Regional => {
                let _ = writeln!(
                    out,
                    "    let provider_id = format!(\"{{region}}/{{name}}\");"
                );
            }
            _ => {
                let _ = writeln!(out, "    let provider_id = name.to_string();");
            }
        }
    }
}

/// Get the GCP resource path segment (e.g., "cryptoKeys", "nodePools").
/// Uses `resource_noun` if set, otherwise falls back to snake_case(model) + "s".
fn resource_noun_plural(m: &ResourceManifest) -> String {
    if let Some(ref noun) = m.resource.resource_noun {
        noun.clone()
    } else {
        let noun = snake_case(&m.resource.sdk_model);
        format!("{noun}s")
    }
}

/// Build the parent expression for resource_name style create calls.
fn build_parent_expr(m: &ResourceManifest) -> String {
    if let Some(ref binding) = m.resource.parent_binding {
        // Nested resource: parent is the parent resource's provider_id (from binding)
        let section = m
            .resource
            .parent_binding_section
            .as_deref()
            .unwrap_or("identity");
        format!(
            "config.require_str(\"/{section}/{binding}\")?.to_string()"
        )
    } else if let Some(ref parent_fmt) = m.resource.parent_format {
        let has_location = parent_fmt.contains("{location}");
        if has_location {
            "format!(\"projects/{}/locations/{}\", self.project_id, self.region)".into()
        } else {
            "format!(\"projects/{}\", self.project_id)".into()
        }
    } else {
        "format!(\"projects/{}\", self.project_id)".into()
    }
}

fn write_state_json(out: &mut String, m: &ResourceManifest, model_var: &str) {
    let sections = group_fields_by_section(&m.fields);

    // Filter labels from state
    if m.fields.contains_key("labels") {
        let _ = writeln!(out, "    let user_labels: serde_json::Map<String, serde_json::Value> = {model_var}.labels");
        let _ = writeln!(out, "        .iter()");
        let _ = writeln!(
            out,
            "        .filter(|(k, _)| k.as_str() != \"managed_by\")"
        );
        let _ = writeln!(
            out,
            "        .map(|(k, v)| (k.clone(), serde_json::json!(v)))"
        );
        let _ = writeln!(out, "        .collect();");
        let _ = writeln!(out);
    }

    let _ = writeln!(out, "    let state = serde_json::json!({{");

    for (section_name, fields) in &sections {
        let _ = writeln!(out, "        \"{section_name}\": {{");
        for (field_name, field) in fields {
            if field.output_only {
                continue;
            }
            let accessor = field_read_accessor(model_var, field_name, field);
            let _ = writeln!(out, "            \"{field_name}\": {accessor},");
        }
        let _ = writeln!(out, "        }},");
    }

    let _ = writeln!(out, "    }});");
    let _ = writeln!(out);
}

fn field_read_accessor(model_var: &str, field_name: &str, field: &FieldDef) -> String {
    let sdk_field = field
        .sdk_field
        .as_deref()
        .unwrap_or(field_name);

    if field_name == "labels" {
        return "user_labels".into();
    }

    if field.optional {
        // Field is Option<T> in the SDK
        match field.field_type.as_str() {
            "String" => format!("{model_var}.{sdk_field}.as_deref().unwrap_or(\"\")"),
            "Bool" => format!("{model_var}.{sdk_field}.unwrap_or(false)"),
            "Integer" | "Integer_u32" | "Integer_u64" => format!("{model_var}.{sdk_field}.unwrap_or(0)"),
            "Float" => format!("{model_var}.{sdk_field}.unwrap_or(0.0)"),
            _ => {
                // Enum, Nested, Vec, HashMap, Duration, Timestamp — serialize via serde.
                // Only attempt serialization if we have a valid type path (meaning the type
                // was successfully resolved). Proto oneofs without Serialize get Null.
                if field.sdk_type_path.is_some() {
                    format!("&{model_var}.{sdk_field}")
                } else {
                    "serde_json::Value::Null".into()
                }
            }
        }
    } else {
        // Field is bare T in the SDK (not wrapped in Option)
        match field.field_type.as_str() {
            "String" => format!("{model_var}.{sdk_field}.as_str()"),
            "Bool" | "Integer" | "Integer_u32" | "Integer_u64" | "Float" => format!("{model_var}.{sdk_field}"),
            _ => {
                if field.sdk_type_path.is_some() {
                    format!("&{model_var}.{sdk_field}")
                } else {
                    "serde_json::Value::Null".into()
                }
            }
        }
    }
}

