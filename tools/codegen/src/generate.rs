/// Convert SDK crate name to Rust module path.
/// "google-cloud-compute-v1" → "google_cloud_compute_v1"
/// "google-cloud-secretmanager-v1" → "google_cloud_secretmanager_v1"
fn sdk_crate_to_mod(sdk_crate: &str) -> String {
    sdk_crate.replace('-', "_")
}

fn schema_fn_name(type_path: &str) -> String {
    format!(
        "{}_schema",
        type_path.replace('.', "_").to_lowercase()
    )
}

fn crud_fn_name(operation: &str, type_path: &str) -> String {
    format!(
        "{operation}_{}",
        type_path.replace('.', "_").to_lowercase()
    )
}

fn snake_case(s: &str) -> String {
    let mut result = String::with_capacity(s.len() + 4);
    for (i, ch) in s.chars().enumerate() {
        if ch.is_uppercase() && i > 0 {
            result.push('_');
        }
        result.push(ch.to_ascii_lowercase());
    }
    result
}

fn capitalize(s: &str) -> String {
    let mut chars = s.chars();
    match chars.next() {
        None => String::new(),
        Some(c) => c.to_uppercase().chain(chars).collect(),
    }
}

fn field_type_expr(type_str: &str, variants: &[String]) -> String {
    match type_str {
        "String" => "crate::provider::FieldType::String".into(),
        "Bool" => "crate::provider::FieldType::Bool".into(),
        "Integer" | "Integer_u32" | "Integer_u64" => "crate::provider::FieldType::Integer".into(),
        "Float" => "crate::provider::FieldType::Float".into(),
        s if s.starts_with("Enum(") => {
            if variants.is_empty() {
                "crate::provider::FieldType::String /* TODO: Enum variants */".into()
            } else {
                let vs: Vec<String> = variants
                    .iter()
                    .map(|v| format!("\"{v}\".into()"))
                    .collect();
                format!(
                    "crate::provider::FieldType::Enum(vec![{}])",
                    vs.join(", ")
                )
            }
        }
        s if s.starts_with("Array(") => {
            let inner = &s[6..s.len() - 1];
            format!(
                "crate::provider::FieldType::Array(Box::new({}))",
                field_type_expr(inner, &[])
            )
        }
        "Record" => "crate::provider::FieldType::Record(vec![])".into(),
        s if s.starts_with("Nested(") => "crate::provider::FieldType::Record(vec![])".into(),
        "Duration" | "Timestamp" | "Bytes" => "crate::provider::FieldType::String".into(),
        _ => format!("crate::provider::FieldType::String /* {type_str} */"),
    }
}

fn toml_to_json_literal(v: &toml::Value) -> String {
    match v {
        toml::Value::String(s) => format!("\"{s}\""),
        toml::Value::Integer(i) => format!("{i}"),
        toml::Value::Float(f) => format!("{f}"),
        toml::Value::Boolean(b) => format!("{b}"),
        _ => "null".into(),
    }
}

/// Group fields by section, preserving field order within each section.
fn group_fields_by_section(
    fields: &BTreeMap<String, FieldDef>,
) -> Vec<(String, Vec<(&String, &FieldDef)>)> {
    let mut sections: BTreeMap<String, Vec<(&String, &FieldDef)>> = BTreeMap::new();
    for (name, field) in fields {
        if field.skip {
            continue;
        }
        sections
            .entry(field.section.clone())
            .or_default()
            .push((name, field));
    }
    // Put "identity" first, then alphabetical
    let mut result = Vec::new();
    if let Some(identity) = sections.remove("identity") {
        result.push(("identity".into(), identity));
    }
    for (section, fields) in sections {
        result.push((section, fields));
    }
    result
}

fn write_field_extraction(out: &mut String, name: &str, field: &FieldDef, sdk_crate_mod: &str) {
    let section = &field.section;
    let path = format!("/{section}/{name}");

    match field.field_type.as_str() {
        "String" => {
            if field.required {
                let _ = writeln!(
                    out,
                    "    let {name} = config.require_str(\"{path}\")?.to_string();"
                );
            } else if let Some(ref default) = field.default {
                let _ = writeln!(
                    out,
                    "    let {name} = config.str_or(\"{path}\", {}).to_string();",
                    toml_to_json_literal(default)
                );
            } else {
                let _ = writeln!(
                    out,
                    "    let {name} = config.optional_str(\"{path}\").map(String::from);"
                );
            }
        }
        "Bool" => {
            if field.required {
                let _ = writeln!(
                    out,
                    "    let {name} = config.require_bool(\"{path}\")?;"
                );
            } else if let Some(ref default) = field.default {
                let _ = writeln!(
                    out,
                    "    let {name} = config.bool_or(\"{path}\", {});",
                    toml_to_json_literal(default)
                );
            } else {
                let _ = writeln!(
                    out,
                    "    let {name} = config.optional_bool(\"{path}\");"
                );
            }
        }
        "Integer" | "Integer_u32" | "Integer_u64" => {
            if field.required {
                let _ = writeln!(
                    out,
                    "    let {name} = config.require_i64(\"{path}\")?;"
                );
            } else if let Some(ref default) = field.default {
                let _ = writeln!(
                    out,
                    "    let {name} = config.i64_or(\"{path}\", {});",
                    toml_to_json_literal(default)
                );
            } else {
                let _ = writeln!(
                    out,
                    "    let {name} = config.optional_i64(\"{path}\");"
                );
            }
        }
        "Float" => {
            // Float extraction — use pointer + as_f64
            let _ = writeln!(
                out,
                "    let {name} = config.pointer(\"{path}\").and_then(|v| v.as_f64());"
            );
        }
        s if s.starts_with("Enum(") => {
            // Enums are extracted as strings from config, then converted via from_name() in the setter
            if field.required {
                let _ = writeln!(
                    out,
                    "    let {name} = config.require_str(\"{path}\")?.to_string();"
                );
            } else {
                let _ = writeln!(
                    out,
                    "    let {name} = config.optional_str(\"{path}\").map(String::from);"
                );
            }
        }
        _ => {
            // Nested, Array, Record, Duration, Timestamp — deserialize from config JSON
            if let Some(ref type_path) = field.sdk_type_path {
                let full_type = type_path.replace("crate::", &format!("{sdk_crate_mod}::"));
                let _ = writeln!(
                    out,
                    "    let {name} = config.pointer(\"{path}\").and_then(|v| serde_json::from_value::<{full_type}>(v.clone()).ok());"
                );
            } else {
                let _ = writeln!(
                    out,
                    "    // TODO: extract {name} ({}) from config[\"{path}\"] — type path unknown",
                    field.field_type
                );
            }
        }
    }
}

fn write_model_setter(out: &mut String, name: &str, setter: &str, field: &FieldDef, sdk_crate_mod: &str) {
    match field.field_type.as_str() {
        "String" => {
            if field.required {
                let _ = writeln!(out, "    model = model.{setter}({name}.clone());");
            } else {
                let _ = writeln!(out, "    if let Some(v) = {name} {{");
                let _ = writeln!(out, "        model = model.{setter}(v);");
                let _ = writeln!(out, "    }}");
            }
        }
        "Bool" => {
            if field.required {
                let _ = writeln!(out, "    model = model.{setter}({name});");
            } else {
                let _ = writeln!(out, "    if let Some(v) = {name} {{");
                let _ = writeln!(out, "        model = model.{setter}(v);");
                let _ = writeln!(out, "    }}");
            }
        }
        "Integer" | "Integer_u32" | "Integer_u64" => {
            // Cast from extracted i64 to the SDK's expected integer type
            let cast_type = match field.field_type.as_str() {
                "Integer_u32" => "u32",
                "Integer_u64" => "u64",
                _ => "i32", // default: most SDK fields use i32
            };
            if field.required {
                let _ = writeln!(out, "    model = model.{setter}({name} as {cast_type});");
            } else {
                let _ = writeln!(out, "    if let Some(v) = {name} {{");
                let _ = writeln!(out, "        model = model.{setter}(v as {cast_type});");
                let _ = writeln!(out, "    }}");
            }
        }
        "Float" => {
            if field.required {
                let _ = writeln!(out, "    model = model.{setter}({name} as f32);");
            } else {
                let _ = writeln!(out, "    if let Some(v) = {name} {{");
                let _ = writeln!(out, "        model = model.{setter}(v as f32);");
                let _ = writeln!(out, "    }}");
            }
        }
        s if s.starts_with("Enum(") => {
            // Convert string to SDK enum via From<&str>
            if let Some(ref type_path) = field.sdk_type_path {
                let full_path = type_path.replace("crate::", &format!("{sdk_crate_mod}::"));
                if field.required {
                    // required field: name was extracted as String
                    let _ = writeln!(out, "    model = model.{setter}({full_path}::from({name}.as_str()));");
                } else {
                    // optional field: name was extracted as Option<String>
                    let _ = writeln!(out, "    if let Some(ref s) = {name} {{");
                    let _ = writeln!(out, "        model = model.{setter}({full_path}::from(s.as_str()));");
                    let _ = writeln!(out, "    }}");
                }
            } else {
                let _ = writeln!(out, "    // TODO: set {name} on model via .{setter}() — enum type path unknown");
            }
        }
        _ => {
            // Nested, Array, Record, Duration, Timestamp — already extracted as Option<T>
            if field.sdk_type_path.is_some() {
                let _ = writeln!(out, "    if let Some(v) = {name} {{");
                let _ = writeln!(out, "        model = model.{setter}(v);");
                let _ = writeln!(out, "    }}");
            } else {
                let _ = writeln!(out, "    // TODO: set {name} on model via .{setter}() — type path unknown");
            }
        }
    }
}

fn write_provider_id_parsing(out: &mut String, m: &ResourceManifest) {
    if m.resource.api_style == "resource_name" {
        // Resource-name style: provider_id IS the full resource name
        // Extract the short name from the last path segment
        let _ = writeln!(
            out,
            "    let name = provider_id.rsplit('/').next().unwrap_or(provider_id).to_string();"
        );
    } else {
        match m.resource.scope.as_str() {
            "zonal" => {
                let _ = writeln!(
                    out,
                    "    let (zone, name) = super::parse_zone_resource(provider_id, &self.region);"
                );
            }
            "regional" => {
                let _ = writeln!(
                    out,
                    "    let (region, name) = super::parse_region_resource(provider_id, &self.region);"
                );
            }
            _ => {
                let _ = writeln!(
                    out,
                    "    let name = provider_id.to_string();"
                );
            }
        }
    }
}

fn write_provider_id_construction(out: &mut String, m: &ResourceManifest) {
    if m.resource.api_style == "resource_name" {
        // Resource-name style: build full resource path
        if let Some(ref parent_fmt) = m.resource.parent_format {
            // Replace {project} and {location} placeholders
            let parent = parent_fmt
                .replace("{project}", "{}")
                .replace("{location}", "{}");
            let noun = snake_case(&m.resource.sdk_model);
            let noun_plural = format!("{noun}s");

            // Build the format string
            let has_project = parent_fmt.contains("{project}");
            let has_location = parent_fmt.contains("{location}");

            if has_project && has_location {
                let _ = writeln!(
                    out,
                    "    let provider_id = format!(\"projects/{{}}/locations/{{}}/{noun_plural}/{{}}\", self.project_id, self.region, name);"
                );
            } else if has_project {
                let _ = writeln!(
                    out,
                    "    let provider_id = format!(\"projects/{{}}/{noun_plural}/{{}}\", self.project_id, name);"
                );
            } else {
                let _ = writeln!(out, "    let provider_id = name.to_string();");
            }
        } else {
            let _ = writeln!(out, "    let provider_id = name.to_string();");
        }
    } else {
        match m.resource.scope.as_str() {
            "zonal" => {
                let _ = writeln!(
                    out,
                    "    let provider_id = format!(\"{{zone}}/{{name}}\");"
                );
            }
            "regional" => {
                let _ = writeln!(
                    out,
                    "    let provider_id = format!(\"{{region}}/{{name}}\");"
                );
            }
            _ => {
                let _ = writeln!(out, "    let provider_id = name.to_string();");
            }
        }
    }
}

/// Build the parent expression for resource_name style create calls.
fn build_parent_expr(m: &ResourceManifest) -> String {
    if let Some(ref parent_fmt) = m.resource.parent_format {
        let has_location = parent_fmt.contains("{location}");
        if has_location {
            "format!(\"projects/{}/locations/{}\", self.project_id, self.region)".into()
        } else {
            "format!(\"projects/{}\", self.project_id)".into()
        }
    } else {
        "format!(\"projects/{}\", self.project_id)".into()
    }
}

fn write_state_json(out: &mut String, m: &ResourceManifest, model_var: &str) {
    let sections = group_fields_by_section(&m.fields);

    // Filter labels from state
    if m.fields.contains_key("labels") {
        let _ = writeln!(out, "    let user_labels: serde_json::Map<String, serde_json::Value> = {model_var}.labels");
        let _ = writeln!(out, "        .iter()");
        let _ = writeln!(
            out,
            "        .filter(|(k, _)| k.as_str() != \"managed_by\")"
        );
        let _ = writeln!(
            out,
            "        .map(|(k, v)| (k.clone(), serde_json::json!(v)))"
        );
        let _ = writeln!(out, "        .collect();");
        let _ = writeln!(out);
    }

    let _ = writeln!(out, "    let state = serde_json::json!({{");

    for (section_name, fields) in &sections {
        let _ = writeln!(out, "        \"{section_name}\": {{");
        for (field_name, field) in fields {
            if field.output_only {
                continue;
            }
            let accessor = field_read_accessor(model_var, field_name, field);
            let _ = writeln!(out, "            \"{field_name}\": {accessor},");
        }
        let _ = writeln!(out, "        }},");
    }

    let _ = writeln!(out, "    }});");
    let _ = writeln!(out);
}

fn field_read_accessor(model_var: &str, field_name: &str, field: &FieldDef) -> String {
    let sdk_field = field
        .sdk_field
        .as_deref()
        .unwrap_or(field_name);

    if field_name == "labels" {
        return "user_labels".into();
    }

    if field.optional {
        // Field is Option<T> in the SDK
        match field.field_type.as_str() {
            "String" => format!("{model_var}.{sdk_field}.as_deref().unwrap_or(\"\")"),
            "Bool" => format!("{model_var}.{sdk_field}.unwrap_or(false)"),
            "Integer" | "Integer_u32" | "Integer_u64" => format!("{model_var}.{sdk_field}.unwrap_or(0)"),
            "Float" => format!("{model_var}.{sdk_field}.unwrap_or(0.0)"),
            _ => {
                // Enum, Nested, Vec, HashMap, Duration, Timestamp — serialize via serde.
                // Only attempt serialization if we have a valid type path (meaning the type
                // was successfully resolved). Proto oneofs without Serialize get Null.
                if field.sdk_type_path.is_some() {
                    format!("&{model_var}.{sdk_field}")
                } else {
                    "serde_json::Value::Null".into()
                }
            }
        }
    } else {
        // Field is bare T in the SDK (not wrapped in Option)
        match field.field_type.as_str() {
            "String" => format!("{model_var}.{sdk_field}.as_str()"),
            "Bool" | "Integer" | "Integer_u32" | "Integer_u64" | "Float" => format!("{model_var}.{sdk_field}"),
            _ => {
                if field.sdk_type_path.is_some() {
                    format!("&{model_var}.{sdk_field}")
                } else {
                    "serde_json::Value::Null".into()
                }
            }
        }
    }
}

